/* ============================================================
   nav.js — Shared navigation for xFACts Secrets Revealed pages
   
   Reads doc-registry.json (generated by the documentation
   pipeline from Component_Registry doc_* columns) and builds
   the navigation bar dynamically.

   Page filenames are derived from pageId by convention:
     Narrative:    {pageId}.html
     CC Guide:     {pageId}-cc.html          (in cc/ subfolder)
     Named CC:     {pageId}-cc-{slug}.html   (in cc/ subfolder)
     Architecture: {pageId}-arch.html        (in arch/ subfolder)
     Reference:    {pageId}-ref.html         (in ref/ subfolder)

   Named CC guide pages are used when a pageId has multiple CC
   guides (e.g., controlcenter-cc-admin.html). The slug and
   display title come from doc_cc_slug and doc_title in the
   registry sections. When any named CC slugs exist for a pageId,
   the standard single-file check ({pageId}-cc.html) is suppressed.

   The index page (sortOrder 0) uses pageId 'index', producing
   index.html naturally via the same convention.

   Child page links are only rendered for the current page,
   and only when the file exists (checked via HEAD request).

   Falls back to a minimal hardcoded nav if JSON fetch fails.

   Version: Tracked in dbo.System_Metadata (component: Documentation.Site)
   ============================================================ */

(function () {
    'use strict';

    // ===== CHILD PAGE TYPES =====
    // Order determines display order in the nav. Add new types here.
    // Named CC guide pages are handled separately — they insert before
    // the -arch entry using registry data rather than this array.
    var CHILD_TYPES = [
        { suffix: '-cc',   folder: 'cc/',   label: 'Control Center' },
        { suffix: '-arch', folder: 'arch/', label: 'Architecture' },
        { suffix: '-ref',  folder: 'ref/',  label: 'Reference' }
    ];

    // ===== MINIMAL FALLBACK =====
    // Used only if doc-registry.json cannot be loaded.
    var FALLBACK_PAGES = [
        { pageId: 'index', title: 'xFACts Secrets Revealed', sortOrder: 0 },
        { pageId: 'engine-room', title: 'The Engine Room', sortOrder: 10 },
        { pageId: 'serverhealth', title: 'Server Health', sortOrder: 20 }
    ];

    // ===== DETECT CURRENT PAGE =====
    var path = window.location.pathname;
    var filename = path.substring(path.lastIndexOf('/') + 1) || 'index.html';
    var isSubfolder = path.indexOf('/ref/') !== -1 ||
                      path.indexOf('/arch/') !== -1 ||
                      path.indexOf('/cc/') !== -1;
    var prefix = isSubfolder ? '../' : '';

    // ===== DERIVE FILENAME FROM PAGE ID =====
    function pageFile(pageId) {
        return pageId + '.html';
    }

    // ===== EXTRACT CC SLUGS FROM PAGE SECTIONS =====
    // Returns array of { slug, title } for sections with ccSlug populated.
    function getCcSlugs(page) {
        var slugs = [];
        if (!page.sections) return slugs;
        for (var i = 0; i < page.sections.length; i++) {
            var s = page.sections[i];
            if (s.ccSlug) {
                slugs.push({ slug: s.ccSlug, title: s.ccTitle || s.ccSlug });
            }
        }
        return slugs;
    }

    // ===== DETECT CURRENT PAGE ID AND TYPE =====
    function detectCurrent(pages) {
        for (var i = 0; i < pages.length; i++) {
            var p = pages[i];
            var narrative = pageFile(p.pageId);

            if (filename === narrative) {
                return { id: p.pageId, type: 'narrative' };
            }

            // Check named CC guide pages first (more specific match)
            var slugs = getCcSlugs(p);
            for (var s = 0; s < slugs.length; s++) {
                var namedCcFile = p.pageId + '-cc-' + slugs[s].slug + '.html';
                if (filename === namedCcFile) {
                    return { id: p.pageId, type: '-cc', slug: slugs[s].slug };
                }
            }

            // Check standard child types
            for (var j = 0; j < CHILD_TYPES.length; j++) {
                var ct = CHILD_TYPES[j];
                var childFile = p.pageId + ct.suffix + '.html';
                if (filename === childFile) {
                    return { id: p.pageId, type: ct.suffix };
                }
            }
        }
        return { id: null, type: 'narrative' };
    }

    // ===== CHECK IF CHILD FILE EXISTS (async) =====
    function checkFileExists(url, callback) {
        var xhr = new XMLHttpRequest();
        xhr.open('HEAD', url, true);
        xhr.onload = function () { callback(xhr.status === 200); };
        xhr.onerror = function () { callback(false); };
        xhr.send();
    }

    // ===== BUILD NAV HTML =====
    // children: optional array of discovered child pages for the current page
    function buildNav(pages, currentChildren) {
        var current = detectCurrent(pages);
        var html = '';
        var shownCount = 0;

        for (var i = 0; i < pages.length; i++) {
            var p = pages[i];
            var isCurrent = (p.pageId === current.id);
            var narrative = pageFile(p.pageId);
            var href = prefix + narrative;
            var label = p.title || p.pageId;

            if (shownCount > 0) {
                html += '<span class="sep">\u2022</span>';
            }

            if (isCurrent && p.sortOrder !== 0 && currentChildren && currentChildren.length > 0) {
                // Active page with discovered children
                html += '<span class="nav-active-group">';

                if (current.type !== 'narrative') {
                    html += '<a href="' + href + '">' + label + '</a>';
                } else {
                    html += '<span class="current">' + label + '</span>';
                }

                for (var k = 0; k < currentChildren.length; k++) {
                    var child = currentChildren[k];
                    html += '<span class="nav-child-line">';
                    html += '<span class="nav-child-arrow">\u21B3</span> ';

                    // Determine if this child is the current page
                    var isCurrentChild = false;
                    if (child.slug && current.slug) {
                        // Named CC guide: match by slug
                        isCurrentChild = (current.type === '-cc' && current.slug === child.slug);
                    } else {
                        // Standard child: match by suffix
                        isCurrentChild = (current.type === child.suffix);
                    }

                    if (isCurrentChild) {
                        html += '<span class="current">' + child.label + '</span>';
                    } else {
                        html += '<a href="' + child.path + '">' + child.label + '</a>';
                    }
                    html += '</span>';
                }

                html += '</span>';
            } else if (isCurrent) {
                // Current page (index, or no children discovered yet)
                html += '<span class="current">' + label + '</span>';
            } else {
                // Inactive parent
                html += '<a href="' + href + '">' + label + '</a>';
            }

            shownCount++;
        }

        return html;
    }

    // ===== DISCOVER CHILD PAGES (async) =====
    // Checks all child types for the current page, then re-renders nav with results.
    // Named CC guide pages (from registry ccSlug data) are checked instead of the
    // standard {pageId}-cc.html when slugs exist for the current pageId.
    function discoverChildren(pages) {
        var current = detectCurrent(pages);
        if (!current.id) return;

        // Find the current page entry
        var currentPage = null;
        for (var i = 0; i < pages.length; i++) {
            if (pages[i].pageId === current.id) { currentPage = pages[i]; break; }
        }
        if (!currentPage || currentPage.sortOrder === 0) return;

        // Determine if this page has named CC guide pages
        var ccSlugs = getCcSlugs(currentPage);
        var hasNamedCc = ccSlugs.length > 0;

        // Build the list of checks to perform
        var checks = [];

        if (hasNamedCc) {
            // Named CC guides — one check per slug, ordered by slug array position
            for (var s = 0; s < ccSlugs.length; s++) {
                checks.push({
                    path: prefix + 'cc/' + currentPage.pageId + '-cc-' + ccSlugs[s].slug + '.html',
                    suffix: '-cc',
                    folder: 'cc/',
                    label: ccSlugs[s].title,
                    slug: ccSlugs[s].slug,
                    order: s  // Named CC guides appear first, in registry order
                });
            }
            // Architecture and Reference follow named CC guides
            for (var j = 1; j < CHILD_TYPES.length; j++) {
                var ct = CHILD_TYPES[j];
                checks.push({
                    path: prefix + ct.folder + currentPage.pageId + ct.suffix + '.html',
                    suffix: ct.suffix,
                    folder: ct.folder,
                    label: ct.label,
                    slug: null,
                    order: ccSlugs.length + (j - 1)
                });
            }
        } else {
            // Standard discovery — check all CHILD_TYPES as before
            for (var j = 0; j < CHILD_TYPES.length; j++) {
                var ct = CHILD_TYPES[j];
                checks.push({
                    path: prefix + ct.folder + currentPage.pageId + ct.suffix + '.html',
                    suffix: ct.suffix,
                    folder: ct.folder,
                    label: ct.label,
                    slug: null,
                    order: j
                });
            }
        }

        // Execute all checks async
        var remaining = checks.length;
        var children = [];

        for (var c = 0; c < checks.length; c++) {
            (function (check) {
                checkFileExists(check.path, function (exists) {
                    if (exists) {
                        children.push({
                            suffix: check.suffix,
                            folder: check.folder,
                            label: check.label,
                            path: check.path,
                            slug: check.slug,
                            order: check.order
                        });
                    }
                    remaining--;
                    if (remaining === 0 && children.length > 0) {
                        children.sort(function (a, b) { return a.order - b.order; });
                        var navHtml = buildNav(pages, children);
                        injectNav(navHtml);
                    }
                });
            })(checks[c]);
        }
    }

    // ===== BUILD HUB CARD GRID =====
    function buildHubCards(pages) {
        var grid = document.querySelector('.module-grid');
        if (!grid) return;

        var html = '';
        for (var i = 0; i < pages.length; i++) {
            var p = pages[i];
            if (p.sortOrder === 0) continue;

            var narrative = pageFile(p.pageId, false);
            var desc = '';

            // Use description from first section
            if (p.sections && p.sections.length > 0 && p.sections[0].description) {
                desc = p.sections[0].description;
            }

            html += '<a class="module-card" href="' + narrative + '">';
            html += '<div class="card-title">' + (p.title || p.pageId) + '</div>';
            if (desc) {
                html += '<div class="card-desc">' + desc + '</div>';
            }
            html += '</a>';
        }

        grid.innerHTML = html;
    }

    // ===== INJECT NAV INTO PAGE =====
    function injectNav(html) {
        var navElement = document.querySelector('.doc-nav');
        if (navElement) {
            navElement.innerHTML = html;
        }

        // Sticky nav (reference pages)
        var stickyNavTop = document.querySelector('.sticky-nav-top');
        if (stickyNavTop) {
            stickyNavTop.innerHTML = html;
            var stickyNav = document.querySelector('.sticky-nav');
            if (stickyNav) {
                var updateScrollPadding = function () {
                    var navHeight = stickyNav.offsetHeight;
                    document.documentElement.style.scrollPaddingTop = (navHeight + 18) + 'px';
                };
                updateScrollPadding();
                window.addEventListener('resize', updateScrollPadding);
            }
        }

        // Section nav (architecture pages)
        var sectionNavTop = document.querySelector('.section-nav-top');
        if (sectionNavTop) {
            sectionNavTop.innerHTML = html;
            var sectionNav = document.querySelector('.section-nav');
            if (sectionNav) {
                var updateScrollPadding = function () {
                    var navHeight = sectionNav.offsetHeight;
                    document.documentElement.style.scrollPaddingTop = (navHeight + 18) + 'px';
                };
                updateScrollPadding();
                window.addEventListener('resize', updateScrollPadding);
            }
        }
    }

    // ===== TRANSFORM REGISTRY TO NAV PAGES =====
    // Extracts primary rows only (pages with sortOrder set), ordered by sortOrder.
    function registryToPages(registry) {
        var pages = [];
        for (var i = 0; i < registry.length; i++) {
            var entry = registry[i];
            if (entry.sortOrder !== null && entry.sortOrder !== undefined) {
                pages.push(entry);
            }
        }
        pages.sort(function (a, b) { return a.sortOrder - b.sortOrder; });
        return pages;
    }

    // ===== LOAD AND RENDER =====
    function loadAndRender(pages) {
        // Pass 1: Render nav immediately with parent links only
        var navHtml = buildNav(pages, null);
        injectNav(navHtml);
        buildHubCards(pages);

        // Pass 2: Discover child pages async, re-render with children when found
        discoverChildren(pages);
    }

    // ===== FETCH REGISTRY =====
    // From pages/: ../data/ddl/   From pages/arch/ or pages/ref/: ../../data/ddl/
    var dataPrefix = isSubfolder ? '../../' : '../';
    var jsonPath = dataPrefix + 'data/ddl/doc-registry.json';

    var xhr = new XMLHttpRequest();
    xhr.open('GET', jsonPath, false);  // Synchronous — nav must render before page content
    try {
        xhr.send();
        if (xhr.status === 200) {
            var registry = JSON.parse(xhr.responseText);
            var pages = registryToPages(registry);
            loadAndRender(pages);
        } else {
            loadAndRender(FALLBACK_PAGES);
        }
    } catch (e) {
        loadAndRender(FALLBACK_PAGES);
    }

    // ===== EXPAND CARD TOGGLE (delegated) =====
    document.addEventListener('click', function(e) {
        var title = e.target.closest('.expand-card-title');
        if (title) {
            var card = title.closest('.expand-card');
            if (card) card.classList.toggle('open');
        }
    });
})();
