function ConvertTo-ConfluenceExpand {
    param([string]$Title, [string]$Body)
    @"
<ac:structured-macro ac:name="expand">
<ac:parameter ac:name="title">$([System.Security.SecurityElement]::Escape($Title))</ac:parameter>
<ac:rich-text-body>
$Body
</ac:rich-text-body>
</ac:structured-macro>
"@
}

function ConvertTo-ConfluenceTable {
    param([string[]]$Headers, [string[][]]$Rows)
    $h = '<table><colgroup>' + ('<col/>' * $Headers.Count) + '</colgroup><tbody>'
    $headerCells = $Headers | ForEach-Object { "<th>$([System.Security.SecurityElement]::Escape($_))</th>" }
    $h += '<tr>' + ($headerCells -join '') + '</tr>'
    foreach ($row in $Rows) {
        $rowCells = $row | ForEach-Object { "<td>$([System.Security.SecurityElement]::Escape($_))</td>" }
        $h += '<tr>' + ($rowCells -join '') + '</tr>'
    }
    $h += '</tbody></table>'
    return $h
}

