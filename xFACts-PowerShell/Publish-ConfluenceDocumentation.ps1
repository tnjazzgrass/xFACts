function Convert-JsonObjectToStorageFormat {
    param($Obj, [string]$TypeLabel)

    $esc = { param($t) [System.Security.SecurityElement]::Escape($t) }
    $name = $Obj.name
    $body = ''

    # Module / Category line
    if ($Obj.module -or $Obj.category) {
        $body += "<p><strong>Module:</strong> $(& $esc $Obj.module) &#160;|&#160; <strong>Category:</strong> $(& $esc $Obj.category)</p>"
    }

    # Description
    if ($Obj.description) {
        $body += "<p>$(& $esc $Obj.description)</p>"
    }

    # Data Flow (always visible)
    if ($Obj.dataFlow) {
        $body += ConvertTo-ConfluenceInfoPanel -Title 'Data Flow' -Body "<p>$(& $esc $Obj.dataFlow)</p>"
    }

    # Collect inner expand sections — joined with <hr/> for visual separation
    $sections = @()

    # --- Fields (tables) ---
    if ($Obj.columns -and $Obj.columns.Count -gt 0) {
        $rows = @()
        foreach ($col in $Obj.columns) {
            $dtype = if ($col.dataType) { $col.dataType.ToUpper() } else { '' }
            if ($col.length) { $dtype += "($($col.length))" }
            $nullable = if ($col.nullable) { 'Yes' } else { 'No' }
            $dv = if ($col.default) { $col.default } else { '' }
            while ($dv.StartsWith('(') -and $dv.EndsWith(')')) { $dv = $dv.Substring(1, $dv.Length - 2) }
            $display = if ($col.identity) { 'IDENTITY' } elseif ($dv) { $dv } else { [char]0x2014 }
            $desc = if ($col.description) { $col.description } else { '' }
            $rows += ,@($col.name, $dtype, $nullable, [string]$display, $desc)
        }
        $fieldHtml = ConvertTo-ConfluenceTable -Headers @('Column','Type','Nullable','Default','Description') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Fields ($($Obj.columns.Count))" -Body $fieldHtml
    }

    # --- Parameters (procs/functions) ---
    if ($Obj.parameters -and $Obj.parameters.Count -gt 0) {
        $rows = @()
        foreach ($p in $Obj.parameters) {
            $dtype = if ($p.dataType) { $p.dataType.ToUpper() } else { '' }
            if ($p.length) { $dtype += "($($p.length))" }
            $opt = if ($p.isOptional) { 'Optional' } else { 'Required' }
            $desc = if ($p.description) { $p.description } else { '' }
            $rows += ,@($p.name, $dtype, $opt, $desc)
        }
        $paramHtml = ConvertTo-ConfluenceTable -Headers @('Parameter','Type','Required','Description') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Parameters ($($Obj.parameters.Count))" -Body $paramHtml
    }

    # --- Indexes ---
    if ($Obj.indexes -and $Obj.indexes.Count -gt 0) {
        $rows = @()
        foreach ($idx in $Obj.indexes) {
            $cols = $(if ($idx.columns) { $idx.columns } else { @() }) -join ', '
            $inc = $(if ($idx.includes) { $idx.includes } else { @() }) -join ', '
            $rows += ,@($idx.name, $(if ($idx.type) { $idx.type } else { '' }), $cols, $inc)
        }
        $idxHtml = ConvertTo-ConfluenceTable -Headers @('Index','Type','Columns','Includes') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Indexes ($($Obj.indexes.Count))" -Body $idxHtml
    }

    # --- Check Constraints ---
    if ($Obj.checkConstraints -and $Obj.checkConstraints.Count -gt 0) {
        $rows = @()
        foreach ($ck in $Obj.checkConstraints) {
            $rows += ,@($ck.name, $(if ($ck.definition) { $ck.definition } else { '' }))
        }
        $ckHtml = ConvertTo-ConfluenceTable -Headers @('Constraint','Definition') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Check Constraints ($($Obj.checkConstraints.Count))" -Body $ckHtml
    }

    # --- Foreign Keys ---
    if ($Obj.foreignKeys -and $Obj.foreignKeys.Count -gt 0) {
        $rows = @()
        foreach ($fk in $Obj.foreignKeys) {
            $colMap = ($fk.columnMappings | ForEach-Object { "$($_.column) -> $($_.referencedColumn)" }) -join ', '
            $rows += ,@($fk.name, $(if ($fk.referencedTable) { $fk.referencedTable } else { '' }), $colMap)
        }
        $fkHtml = ConvertTo-ConfluenceTable -Headers @('Foreign Key','References','Mapping') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Foreign Keys ($($Obj.foreignKeys.Count))" -Body $fkHtml
    }

    # --- Design Notes ---
    if ($Obj.designNotes -and $Obj.designNotes.Count -gt 0) {
        $dnHtml = ''
        foreach ($note in $Obj.designNotes) {
            $dnHtml += "<p><strong>$(& $esc $note.topic)</strong></p>"
            if ($note.summary) { $dnHtml += "<p><em>$(& $esc $note.summary)</em></p>" }
            $dnHtml += "<p>$(& $esc $note.note)</p>"
        }
        $sections += ConvertTo-ConfluenceExpand -Title "Design Notes ($($Obj.designNotes.Count))" -Body $dnHtml
    }

    # --- Status Values ---
    if ($Obj.statusValues -and $Obj.statusValues.Count -gt 0) {
        $byCol = @{}
        foreach ($sv in $Obj.statusValues) {
            $col = if ($sv.column) { $sv.column } else { 'status' }
            if (-not $byCol.ContainsKey($col)) { $byCol[$col] = @() }
            $byCol[$col] += $sv
        }
        $svHtml = ''
        foreach ($col in $byCol.Keys) {
            $svHtml += "<p><strong>Column: $(& $esc $col)</strong></p>"
            $rows = @()
            foreach ($v in $byCol[$col]) {
                $rows += ,@($(if ($v.value) { $v.value } else { '' }), $(if ($v.meaning) { $v.meaning } else { '' }))
            }
            $svHtml += ConvertTo-ConfluenceTable -Headers @('Value','Meaning') -Rows $rows
        }
        $sections += ConvertTo-ConfluenceExpand -Title "Status Values ($($Obj.statusValues.Count))" -Body $svHtml
    }

    # --- Common Queries ---
    if ($Obj.queries -and $Obj.queries.Count -gt 0) {
        $qHtml = ''
        foreach ($q in $Obj.queries) {
            $qHtml += "<p><strong>$(& $esc $q.name)</strong></p>"
            if ($q.description) { $qHtml += "<p><em>$(& $esc $q.description)</em></p>" }
            $qHtml += ConvertTo-ConfluenceCodeBlock -Code $q.sql
        }
        $sections += ConvertTo-ConfluenceExpand -Title "Common Queries ($($Obj.queries.Count))" -Body $qHtml
    }

    # --- Relationship Notes ---
    if ($Obj.relationshipNotes -and $Obj.relationshipNotes.Count -gt 0) {
        $relHtml = ''
        foreach ($rel in $Obj.relationshipNotes) {
            $relHtml += "<p><strong>$(& $esc $rel.relatedObject)</strong></p>"
            $relHtml += "<p>$(& $esc $rel.note)</p>"
        }
        $sections += ConvertTo-ConfluenceExpand -Title "Relationships ($($Obj.relationshipNotes.Count))" -Body $relHtml
    }

    # Join sections with horizontal rule separators
    $body += $sections -join '<hr/>'

    return ConvertTo-ConfluenceExpand -Title "$name ($TypeLabel)" -Body $body
}

function Convert-JsonToRefStorageFormat {
    param($JsonData)

    $page = ''
    $page += '<p><em>Auto-generated from xFACts Object_Metadata and system catalog.</em></p><hr/>'

    # Collect all top-level object sections — joined with <hr/> for visual separation
    $objects = @()

    foreach ($t in @($JsonData.tables)) {
        if (-not $t) { continue }
        $objects += Convert-JsonObjectToStorageFormat -Obj $t -TypeLabel 'Table'
    }
    foreach ($p in @($JsonData.procedures)) {
        if (-not $p) { continue }
        $objects += Convert-JsonObjectToStorageFormat -Obj $p -TypeLabel 'Procedure'
    }
    foreach ($tr in @($JsonData.triggers)) {
        if (-not $tr) { continue }
        $objects += Convert-JsonObjectToStorageFormat -Obj $tr -TypeLabel 'Trigger'
    }
    foreach ($f in @($JsonData.functions)) {
        if (-not $f) { continue }
        $objects += Convert-JsonObjectToStorageFormat -Obj $f -TypeLabel 'Function'
    }
    foreach ($v in @($JsonData.views)) {
        if (-not $v) { continue }
        $objects += Convert-JsonObjectToStorageFormat -Obj $v -TypeLabel 'View'
    }
    foreach ($s in @($JsonData.scripts)) {
        if (-not $s) { continue }
        $objects += Convert-JsonObjectToStorageFormat -Obj $s -TypeLabel 'Script'
    }

    $page += ($objects -join '<hr/>') + '<hr/>'

    return $page
}

function Convert-CcGuideToStorageFormat {
    param([string]$HtmlContent, [string]$PageTitle)

    # =============================================
    # Standalone converter for CC guide pages.
    # Extracts: About intro, Key flip cards, Guide sections.
    # Strips: Header, nav, mockup, sidebar items, footer, slideout, scripts.
    # Does NOT pass through Convert-HtmlToStorageFormat.
    # =============================================

    $body = ''

    # --- Static reference banner ---
    $body += '<ac:structured-macro ac:name="info"><ac:rich-text-body><p>This page is a static reference version. The interactive guide with page mockup and guided tour is available on the xFACts documentation site.</p></ac:rich-text-body></ac:structured-macro>'

    # --- Extract "About This Page" intro paragraphs ---
    if ($HtmlContent -match '(?s)<div class="sidebar-intro">\s*<h3>[^<]*</h3>\s*(.*?)\s*</div>') {
        $aboutIntro = $Matches[1].Trim()
        # Entity cleanup on intro
        $aboutIntro = $aboutIntro -replace '&mdash;', '&#8212;'
        $aboutIntro = $aboutIntro -replace '&rsquo;', '&#8217;'
        $aboutIntro = $aboutIntro -replace '&ldquo;', '&#8220;'
        $aboutIntro = $aboutIntro -replace '&rdquo;', '&#8221;'
        $aboutIntro = $aboutIntro -replace '&rarr;', '&#8594;'
        $aboutIntro = $aboutIntro -replace '&larr;', '&#8592;'
        $aboutIntro = $aboutIntro -replace '&nbsp;', '&#160;'
        $aboutIntro = $aboutIntro -replace '&hellip;', '&#8230;'
        $aboutIntro = $aboutIntro -replace '&ndash;', '&#8211;'
        $aboutIntro = $aboutIntro -replace '&bull;', '&#8226;'
        $aboutIntro = $aboutIntro -replace '&middot;', '&#183;'
        $aboutIntro = $aboutIntro -replace '&times;', '&#215;'
        $aboutIntro = [regex]::Replace($aboutIntro, '&(?!amp;|#\d+;|#x[\da-fA-F]+;|\w+;)', '&amp;')
        $body += "`n$aboutIntro"
    }

    # --- Extract Key flip cards → table ---
    $keyCards = [regex]::Matches($HtmlContent, '(?s)<div class="key-flip-card">.*?<span class="key-flip-label">(.*?)</span>.*?<div class="key-flip-desc">(.*?)</div>')
    if ($keyCards.Count -gt 0) {
        $body += "`n<h2>Key to the Page</h2>"
        $body += "`n<table><colgroup><col/><col/></colgroup><tbody>"
        $body += '<tr><th>Element</th><th>Description</th></tr>'
        foreach ($card in $keyCards) {
            $label = $card.Groups[1].Value.Trim()
            $desc = $card.Groups[2].Value.Trim()
            # Strip any HTML tags from inside descriptions (icon markup, etc.)
            $desc = $desc -replace '<[^>]+>', ''
            # Entity cleanup
            $desc = $desc -replace '&mdash;', '&#8212;'
            $desc = $desc -replace '&rsquo;', '&#8217;'
            $desc = $desc -replace '&ldquo;', '&#8220;'
            $desc = $desc -replace '&rdquo;', '&#8221;'
            $desc = $desc -replace '&rarr;', '&#8594;'
            $desc = $desc -replace '&larr;', '&#8592;'
            $desc = $desc -replace '&nbsp;', '&#160;'
            $desc = $desc -replace '&hellip;', '&#8230;'
            $desc = $desc -replace '&ndash;', '&#8211;'
            $desc = $desc -replace '&bull;', '&#8226;'
            $desc = $desc -replace '&middot;', '&#183;'
            $desc = $desc -replace '&times;', '&#215;'
            $desc = [regex]::Replace($desc, '&(?!amp;|#\d+;|#x[\da-fA-F]+;|\w+;)', '&amp;')
            $body += "<tr><td><strong>$label</strong></td><td>$desc</td></tr>"
        }
        $body += '</tbody></table>'
    }

    # --- Extract Guide sections → expand macros ---
    if ($HtmlContent -match '(?s)<div class="cc-guide-content">(.*?)(?=\s*<div class="doc-footer")') {
        $ccGuideBlock = $Matches[1]

        # Split on guide-section boundaries (avoids nested div regex problems)
        $sectionChunks = [regex]::Split($ccGuideBlock, '<div class="guide-section" id="section-\d+">\s*<div class="section">\s*')

        $body += "`n<h2>Sections and Features at a Glance</h2>"

        foreach ($chunk in $sectionChunks) {
            if ([string]::IsNullOrWhiteSpace($chunk)) { continue }
            # Must contain an h2 to be a real section
            if ($chunk -notmatch '<h2>') { continue }

            # Trim trailing </div></div> from each chunk
            $sectionContent = $chunk -replace '\s*</div>\s*</div>\s*$', ''

            # Extract title from h2, strip section-number span
            $sectionTitle = ''
            if ($sectionContent -match '<h2>(?:\s*<span class="section-number">\d+</span>\s*)?(.*?)</h2>') {
                $sectionTitle = $Matches[1].Trim()
            }

            # Remove h2 from content (it becomes the expand title)
            $sectionContent = $sectionContent -replace '(?s)<h2>.*?</h2>\s*', ''

            # Convert callout divs to Confluence macros BEFORE stripping remaining divs
            $sectionContent = [regex]::Replace($sectionContent, '(?s)<div class="callout info">\s*(.*?)\s*</div>', {
                param($m)
                "<ac:structured-macro ac:name=`"info`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
            })
            $sectionContent = [regex]::Replace($sectionContent, '(?s)<div class="callout tip">\s*(.*?)\s*</div>', {
                param($m)
                "<ac:structured-macro ac:name=`"tip`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
            })
            $sectionContent = [regex]::Replace($sectionContent, '(?s)<div class="callout warning">\s*(.*?)\s*</div>', {
                param($m)
                "<ac:structured-macro ac:name=`"warning`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
            })

            # Strip remaining div tags (structural wrappers only at this point)
            $sectionContent = $sectionContent -replace '<div[^>]*>', ''
            $sectionContent = $sectionContent -replace '</div>', ''

            # XHTML compliance — self-close bare <br> tags
            $sectionContent = $sectionContent -replace '<br\s*(?!/)>', '<br/>'

            # Entity cleanup
            $sectionContent = $sectionContent -replace '&mdash;', '&#8212;'
            $sectionContent = $sectionContent -replace '&rsquo;', '&#8217;'
            $sectionContent = $sectionContent -replace '&ldquo;', '&#8220;'
            $sectionContent = $sectionContent -replace '&rdquo;', '&#8221;'
            $sectionContent = $sectionContent -replace '&rarr;', '&#8594;'
            $sectionContent = $sectionContent -replace '&larr;', '&#8592;'
            $sectionContent = $sectionContent -replace '&nbsp;', '&#160;'
            $sectionContent = $sectionContent -replace '&hellip;', '&#8230;'
            $sectionContent = $sectionContent -replace '&ndash;', '&#8211;'
            $sectionContent = $sectionContent -replace '&bull;', '&#8226;'
            $sectionContent = $sectionContent -replace '&middot;', '&#183;'
            $sectionContent = $sectionContent -replace '&times;', '&#215;'
            $sectionContent = [regex]::Replace($sectionContent, '&(?!amp;|#\d+;|#x[\da-fA-F]+;|\w+;)', '&amp;')

            # Strip inline styles with CSS variables
            $sectionContent = $sectionContent -replace 'style="color: var\([^)]*\);[^"]*"', ''
            $sectionContent = $sectionContent -replace 'style="[^"]*var\([^)]*\)[^"]*"', ''

            # Clean whitespace
            $sectionContent = $sectionContent -replace '(?m)^\s*$\n', ''
            $sectionContent = $sectionContent.Trim()

            # Build expand macro (following ref page pattern)
            $escapedTitle = [System.Security.SecurityElement]::Escape($sectionTitle)
            $body += "`n<hr/><ac:structured-macro ac:name=`"expand`"><ac:parameter ac:name=`"title`">$escapedTitle</ac:parameter><ac:rich-text-body>`n$sectionContent`n</ac:rich-text-body></ac:structured-macro>"
        }

        # Trailing separator after last expand
        $body += "`n<hr/>"
    }

    # Whitespace cleanup
    $body = $body -replace '(?m)^\s*$\n', ''
    $body = $body.Trim()

    return $body
}

