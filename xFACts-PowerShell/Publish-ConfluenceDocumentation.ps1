            $sectionContent = $sectionContent -replace '(?s)<h2>.*?</h2>\s*', ''

            # Convert callout divs to Confluence macros BEFORE stripping remaining divs
            $sectionContent = [regex]::Replace($sectionContent, '(?s)<div class="callout info">\s*(.*?)\s*</div>', {
