        $outFile = Join-Path $ExportPath $fileName
        $md | Out-File -FilePath $outFile -Encoding UTF8 -Force
        Write-Host "  Exported: $($md.Length) chars" -ForegroundColor Gray
    }
    Write-Host ""
}

