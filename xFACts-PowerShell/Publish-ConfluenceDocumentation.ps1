# Converts a narrative documentation HTML page into Confluence Storage Format.
function Convert-HtmlToStorageFormat {
    param([string]$HtmlContent, [string]$PageTitle)

    $body = $HtmlContent

    # Extract body content inside page-wrapper
    if ($body -match '(?s)<div class="page-wrapper">(.*)</div>\s*<script') {
        $body = $Matches[1]
    }

    # -- PHASE 1: Remove non-Confluence elements --

    # These consume the div entirely (opening + content + closing)

    # Page header (has nested subtitle div)
    $body = $body -replace '(?s)<div class="page-header">\s*<h1>.*?</h1>\s*<div class="subtitle">.*?</div>\s*</div>', ''
    # Navigation bar
    $body = $body -replace '(?s)<div class="doc-nav">.*?</div>', ''
    # Footer (with nested p tags)
    $body = $body -replace '(?s)<div class="doc-footer">\s*(?:<p[^>]*>.*?</p>\s*)*</div>', ''
    # Ref page link div
    $body = $body -replace '(?s)<div style="text-align: center;">\s*<a[^>]*class="ref-link"[^>]*>.*?</a>\s*</div>', ''
    # HTML comments
    $body = $body -replace '(?s)<!--.*?-->', ''
    # Script tags
    $body = $body -replace '(?s)<script[^>]*>.*?</script>', ''

    # -- PHASE 2: Convert special divs to Confluence macros --

    # Each conversion consumes the div entirely

    # Context-bar -> info panel
    $body = [regex]::Replace($body, '(?s)<div class="context-bar">\s*(.*?)\s*</div>', {
        param($m)
        $content = $m.Groups[1].Value.Trim()
        "<ac:structured-macro ac:name=`"info`"><ac:parameter ac:name=`"title`">Overview</ac:parameter><ac:rich-text-body>`n$content`n</ac:rich-text-body></ac:structured-macro>"
    })

    # Callout info
    $body = [regex]::Replace($body, '(?s)<div class="callout info">\s*(.*?)\s*</div>', {
        param($m)
        "<ac:structured-macro ac:name=`"info`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
    })

    # Callout tip
    $body = [regex]::Replace($body, '(?s)<div class="callout tip">\s*(.*?)\s*</div>', {
        param($m)
        "<ac:structured-macro ac:name=`"tip`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
    })

    # Callout warning
    $body = [regex]::Replace($body, '(?s)<div class="callout warning">\s*(.*?)\s*</div>', {
        param($m)
        "<ac:structured-macro ac:name=`"warning`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
    })

    # Callout story -> note panel
    $body = [regex]::Replace($body, '(?s)<div class="callout story">\s*(.*?)\s*</div>', {
        param($m)
        "<ac:structured-macro ac:name=`"note`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
    })

    # Flow diagrams -> simple table (supports flow-step and flow-fork elements)
    while ($body.Contains('<div class="flow-diagram">')) {
        $fdStart = $body.IndexOf('<div class="flow-diagram">')
        # Find matching close by depth counting
        $fdDepth = 0; $fdSearch = $fdStart; $fdEnd = -1
        while ($fdSearch -lt $body.Length) {
            $fdOpen = $body.IndexOf('<div', $fdSearch + 1)
            $fdClose = $body.IndexOf('</div>', $fdSearch + 1)
            if ($fdClose -eq -1) { break }
            if ($fdSearch -eq $fdStart) { $fdDepth = 1; $fdSearch = $fdStart + 1; continue }
            if ($fdOpen -ne -1 -and $fdOpen -lt $fdClose) { $fdDepth++; $fdSearch = $fdOpen + 1 }
            else { $fdDepth--; if ($fdDepth -eq 0) { $fdEnd = $fdClose + 6; break }; $fdSearch = $fdClose + 1 }
        }
        if ($fdEnd -eq -1) { break }

        $fdBlock = $body.Substring($fdStart, $fdEnd - $fdStart)

        # Extract caption
        $fdCaption = ''
        if ($fdBlock -match '<div class="flow-caption">(.*?)</div>') { $fdCaption = $Matches[1].Trim() }

        # Find steps and forks by position
        $fdCells = @()
        $fdStepMatches = [regex]::Matches($fdBlock, '<div class="flow-step[^"]*">(.*?)</div>')
        $fdForkMatches = [regex]::Matches($fdBlock, '(?s)<div class="flow-fork">')

        $fdAll = @()
        foreach ($fs in $fdStepMatches) {
            $fdAll += [PSCustomObject]@{ Type = 'step'; Index = $fs.Index; Text = ($fs.Groups[1].Value -replace '<br\s*/?>', ' ') }
        }
        foreach ($ff in $fdForkMatches) {
            $forkChunk = $fdBlock.Substring($ff.Index, [Math]::Min(500, $fdBlock.Length - $ff.Index))
            $forkBranches = [regex]::Matches($forkChunk, '<div class="flow-branch[^"]*">(.*?)</div>')
            $brLabels = @($forkBranches | ForEach-Object { ($_.Groups[1].Value -replace '<br\s*/?>', ' ').Trim() })
            $fdAll += [PSCustomObject]@{ Type = 'fork'; Index = $ff.Index; Branches = $brLabels }
        }
        $fdAll = $fdAll | Sort-Object Index

        foreach ($fe in $fdAll) {
            if ($fe.Type -eq 'step') {
                $fdCells += "<td><strong>$($fe.Text)</strong></td>"
            } elseif ($fe.Type -eq 'fork') {
                $fdCells += "<td><strong>$($fe.Branches -join ' / ')</strong></td>"
            }
        }

        $fdRowCells = @()
        for ($i = 0; $i -lt $fdCells.Count; $i++) {
            $fdRowCells += $fdCells[$i]
            if ($i -lt $fdCells.Count - 1) { $fdRowCells += '<td>&#8594;</td>' }
        }
        $fdTable = "<table><tbody><tr>$($fdRowCells -join '')</tr></tbody></table>"
        if ($fdCaption) { $fdTable += "<p><em>$fdCaption</em></p>" }

        $body = $body.Substring(0, $fdStart) + $fdTable + $body.Substring($fdEnd)
    }

    # Pattern-flow diagrams (index.html)
    $body = [regex]::Replace($body, '(?s)<div class="pattern-flow">\s*(.*?)\s*</div>', {
        param($m)
        $content = $m.Groups[1].Value
        $steps = [regex]::Matches($content, '<span class="pattern-step[^"]*">(.*?)</span>')
        $cells = @()
        foreach ($step in $steps) { $cells += "<td><strong>$($step.Groups[1].Value)</strong></td>" }
        $rowCells = @()
        for ($i = 0; $i -lt $cells.Count; $i++) {
            $rowCells += $cells[$i]
            if ($i -lt $cells.Count - 1) { $rowCells += '<td>&#8594;</td>' }
        }
        "<table><tbody><tr>$($rowCells -join '')</tr></tbody></table>"
    })

    # Side-by-side -> two-column table
    $body = [regex]::Replace($body, '(?s)<div class="side-by-side">\s*<div>\s*(.*?)\s*</div>\s*<div>\s*(.*?)\s*</div>\s*</div>', {
        param($m)
        $left = $m.Groups[1].Value.Trim()
        $right = $m.Groups[2].Value.Trim()
        "<table><colgroup><col/><col/></colgroup><tbody><tr><td>$left</td><td>$right</td></tr></tbody></table>"
    })

    # Module-grid cards -> simple list (index.html)
    # Use greedy (.*) to capture all nested divs inside the grid
    $body = [regex]::Replace($body, '(?s)<div class="module-grid">\s*(.*)\s*</div>\s*</div>', {
        param($m)
        $gridContent = $m.Groups[1].Value
        $cards = [regex]::Matches($gridContent, '(?s)<a class="module-card"[^>]*>\s*<div class="card-title">(.*?)</div>\s*<div class="card-desc">(.*?)</div>\s*<span class="card-tag[^"]*">(.*?)</span>\s*</a>')
        $result = ''
        foreach ($card in $cards) {
            $title = $card.Groups[1].Value
            $desc = $card.Groups[2].Value
            $tag = $card.Groups[3].Value
            $result += "<p><strong>$title</strong> ($tag)<br/>$desc</p>"
        }
        $result
    })

    # Architecture diagram (index.html) - extract text content from nested arch-box divs
    # Greedy match to consume all nested content including diagram-title
    $body = [regex]::Replace($body, '(?s)<div class="diagram-container">(.*?)<div class="diagram-title">(.*?)</div>\s*</div>', {
        param($m)
        $content = $m.Groups[1].Value
        $diagTitle = $m.Groups[2].Value
        # Extract arch-box text
        $boxes = [regex]::Matches($content, '(?s)<div class="arch-box[^"]*">\s*(.*?)\s*</div>')
        $cells = @()
        foreach ($box in $boxes) {
            $text = $box.Groups[1].Value
            $text = $text -replace '(?s)<[^>]+>', ' '
            $text = $text -replace '\s+', ' '
            $text = $text.Trim()
            $cells += "<td><strong>$text</strong></td>"
        }
        $rowCells = @()
        for ($i = 0; $i -lt $cells.Count; $i++) {
            $rowCells += $cells[$i]
            if ($i -lt $cells.Count - 1) { $rowCells += '<td>&#8594;</td>' }
        }
        $table = "<table><tbody><tr>$($rowCells -join '')</tr></tbody></table>"
        if ($diagTitle) { $table += "<p><em>$diagTitle</em></p>" }
        $table
    })

    # -- PHASE 2.5: XHTML compliance before stripping remaining divs --

    # Must happen before Phase 3 so bare <br> inside surviving spans are fixed

    $body = $body -replace '<br\s*(?!/)>', '<br/>'

    # Strip orphaned span tags with classes/styles (visual-only after div removal)
    $body = $body -replace '<span class="arch-arrow"[^>]*>.*?</span>', ''
    $body = $body -replace '<span class="pattern-arrow"[^>]*>.*?</span>', ''
    $body = $body -replace '<span class="fac"[^>]*>.*?</span>', ''

    # -- PHASE 3: Strip ALL remaining div tags --

    # Only section wrappers and structural divs should be left

    $body = $body -replace '<div[^>]*>', ''
    $body = $body -replace '</div>', ''

    # -- PHASE 4: Clean up tables, links, entities --

    $body = $body -replace 'class="info-table"', ''
    $body = $body -replace 'class="tech-table"', ''
    $body = $body -replace '<span class="color-sample"[^>]*></span>', ''
    $body = $body -replace '<a[^>]*class="ref-inline"[^>]*><code>([^<]*)</code></a>', '<code>$1</code>'

    # Muted paragraphs - wrap content in <em> with proper closure
    $body = $body -replace '<p class="muted">(.*?)</p>', '<p><em>$1</em></p>'

    # XHTML compliance - self-close bare <br> tags
    $body = $body -replace '<br\s*(?!/)>', '<br/>'

    # Entity cleanup
    $body = $body -replace '&mdash;', '&#8212;'
    $body = $body -replace '&rarr;', '&#8594;'
    $body = $body -replace '&larr;', '&#8592;'
    $body = $body -replace '&nbsp;', '&#160;'
    $body = $body -replace '&hellip;', '&#8230;'
    $body = $body -replace '&ndash;', '&#8211;'

    # XHTML compliance - escape bare ampersands in URLs (e.g., mailto query strings)
    # Matches & not already followed by amp; or #digits; or #x hex; or a named entity ending in ;
    $body = [regex]::Replace($body, '&(?!amp;|#\d+;|#x[\da-fA-F]+;|\w+;)', '&amp;')

    # Strip inline styles with CSS variables
    $body = $body -replace 'style="color: var\([^)]*\);[^"]*"', ''
    $body = $body -replace 'style="[^"]*var\([^)]*\)[^"]*"', ''

    # Whitespace cleanup
    $body = $body -replace '(?m)^\s*$\n', ''
    $body = $body.Trim()

    return $body
}

# Builds a PlantUML entity-relationship diagram from a schema JSON object.
function Convert-JsonToPlantUmlErd {
    param(
        $JsonData,
        [string]$SchemaName,
        [string]$CategoryFilter
    )

    # Filter tables by category if specified
    $tables = @($JsonData.tables | Where-Object { $_ })
    if ($CategoryFilter) {
        $tables = @($tables | Where-Object { $_.category -eq $CategoryFilter })
    }

    if ($tables.Count -eq 0) { return '' }

    $uml = "@startuml ${SchemaName}_ERD`n"
    $uml += "skinparam linetype ortho`n"
    $uml += "skinparam defaultFontSize 11`n"
    $uml += "skinparam nodesep 20`n"
    $uml += "skinparam ranksep 40`n"
    $uml += "skinparam entity {`n"
    $uml += "    BackgroundColor #ffffff`n"
    $uml += "    BorderColor #666666`n"
    $uml += "}`n`n"

    # Build table names set for FK filtering
    $tableNames = @{}
    foreach ($t in $tables) { $tableNames[$t.name] = $true }

    # Emit entities with PK/FK columns only (matches web ERD keys-only approach)
    foreach ($t in $tables) {
        $uml += "entity ""$($t.name)"" {`n"

        # Find PK columns from indexes
        $pkCols = @()
        foreach ($idx in @($t.indexes)) {
            if ($idx -and $idx.isPrimaryKey) {
                if ($idx.keyColumns -is [array]) { $pkCols = $idx.keyColumns }
                else { $pkCols = @($idx.keyColumns) }
            }
        }

        # Find FK columns
        $fkCols = @()
        foreach ($fk in @($t.foreignKeys)) {
            if ($fk -and $fk.columnMappings) {
                foreach ($cm in $fk.columnMappings) {
                    $fkCols += $cm.column
                }
            }
        }

        # Emit PK columns
        foreach ($col in $pkCols) {
            $uml += "    * $col <<PK>>`n"
        }
        $uml += "    --`n"

        # Emit FK columns (that aren't also PK)
        foreach ($col in $fkCols) {
            if ($col -notin $pkCols) {
                $uml += "    $col <<FK>>`n"
            }
        }

        $uml += "}`n`n"
    }

    # Emit relationships from foreign keys
    foreach ($t in $tables) {
        foreach ($fk in @($t.foreignKeys)) {
            if (-not $fk) { continue }
            # Extract referenced table name (may be schema-qualified)
            $refTable = $fk.referencedTable
            if ($refTable -match '\.(.+)$') { $refTable = $Matches[1] }

            # Only draw relationship if both tables are in the filtered set
            if ($tableNames.ContainsKey($refTable)) {
                $uml += """$refTable"" ||--o{ ""$($t.name)""`n"
            }
        }
    }

    $uml += "`n@enduml"
    return $uml
}

# Converts an architecture HTML page to Storage Format, rendering ERDs, tech-tips, and diagram placeholders as Confluence macros before the standard narrative phases.
function Convert-ArchHtmlToStorageFormat {
    param(
        [string]$HtmlContent,
        [string]$PageTitle,
        # Schema name -> parsed JSON data.
        [hashtable]$JsonDataCache
    )

    $body = $HtmlContent

    # Extract body content inside page-wrapper
    if ($body -match '(?s)<div class="page-wrapper">(.*)</div>\s*<script') {
        $body = $Matches[1]
    }

    # -- PHASE 0: Architecture-specific element conversion --

    # Must happen before the generic phases strip divs

    # Section-nav -> strip entirely (Confluence has TOC macro)
    # Match the known structure: section-nav containing section-nav-top and section-nav-links
    $body = $body -replace '(?s)<div class="section-nav">\s*<div class="section-nav-top"></div>\s*<div class="section-nav-links">.*?</div>\s*</div>', ''

    # ERD divs -> PlantUML macro
    # Match: <div class="erd-root" data-schema="X"></div>
    # Match: <div class="erd-root" data-schema="X" data-category="Y"></div>
    $body = [regex]::Replace($body, '<div class="erd-root"\s+data-schema="([^"]+)"(?:\s+data-category="([^"]*)")?\s*>\s*</div>', {
        param($m)
        $schema = $m.Groups[1].Value
        $category = $m.Groups[2].Value

        # Look up JSON data from cache
        if ($JsonDataCache -and $JsonDataCache.ContainsKey($schema)) {
            $uml = Convert-JsonToPlantUmlErd -JsonData $JsonDataCache[$schema] -SchemaName $schema -CategoryFilter $category
            if ($uml) {
                return @"
<ac:structured-macro ac:name="plantuml">
<ac:plain-text-body><![CDATA[$uml]]></ac:plain-text-body>
</ac:structured-macro>
"@
            }
        }

        # Fallback if JSON not available
        $label = $schema
        if ($category) { $label += " - $category" }
        return "<p><em>ERD: $label (see Control Center for interactive diagram)</em></p>"
    })

    # Tech-tip divs -> Confluence tip panels
    # These can contain nested <strong> and <p> tags
    $body = [regex]::Replace($body, '(?s)<div class="tech-tip">\s*(.*?)\s*</div>', {
        param($m)
        $content = $m.Groups[1].Value.Trim()
        "<ac:structured-macro ac:name=`"tip`"><ac:rich-text-body>`n$content`n</ac:rich-text-body></ac:structured-macro>"
    })

    # Expand cards -> Confluence expand macros (with separator lines)
    $body = [regex]::Replace($body, '(?s)<div class="expand-card">\s*<div class="expand-card-title">(.*?)</div>\s*<div class="expand-card-body">(.*?)</div>\s*</div>', {
        param($m)
        $title = $m.Groups[1].Value.Trim()
        $cardBody = $m.Groups[2].Value.Trim()
        $escaped = [System.Security.SecurityElement]::Escape($title)
        "<hr/><ac:structured-macro ac:name=`"expand`"><ac:parameter ac:name=`"title`">$escaped</ac:parameter><ac:rich-text-body>`n$cardBody`n</ac:rich-text-body></ac:structured-macro><hr/>"
    })

    # Diagram placeholders -> Confluence note panels
    $body = [regex]::Replace($body, '(?s)<div class="diagram-placeholder">\s*<div class="placeholder-label">(.*?)</div>\s*<div class="placeholder-desc">(.*?)</div>\s*</div>', {
        param($m)
        $label = $m.Groups[1].Value.Trim()
        $desc = $m.Groups[2].Value.Trim()
        "<ac:structured-macro ac:name=`"note`"><ac:parameter ac:name=`"title`">$label</ac:parameter><ac:rich-text-body>`n<p>$desc</p>`n</ac:rich-text-body></ac:structured-macro>"
    })

    # Architecture flow diagrams -> Confluence table with arrows
    # Handles arch-flow-step (with title + detail), arch-flow-fork (branch outcomes),
    # arch-flow-arrow, and arch-flow-caption.
    # Uses index-based extraction to handle nested divs correctly.
    while ($body.Contains('<div class="arch-flow">')) {
        $startIdx = $body.IndexOf('<div class="arch-flow">')
        # Find matching closing </div> by counting depth
        $depth = 0
        $searchIdx = $startIdx
        $endIdx = -1
        while ($searchIdx -lt $body.Length) {
            $nextOpen = $body.IndexOf('<div', $searchIdx + 1)
            $nextClose = $body.IndexOf('</div>', $searchIdx + 1)
            if ($nextClose -eq -1) { break }
            if ($searchIdx -eq $startIdx) { $depth = 1; $searchIdx = $startIdx + 1; continue }
            if ($nextOpen -ne -1 -and $nextOpen -lt $nextClose) {
                $depth++
                $searchIdx = $nextOpen + 1
            } else {
                $depth--
                # 6 = length of the closing div tag.
                if ($depth -eq 0) { $endIdx = $nextClose + 6; break }
                $searchIdx = $nextClose + 1
            }
        }
        # Safety: couldn't find matching close.
        if ($endIdx -eq -1) { break }

        $flowBlock = $body.Substring($startIdx, $endIdx - $startIdx)

        # Extract caption if present
        $caption = ''
        if ($flowBlock -match '<div class="arch-flow-caption">(.*?)</div>') {
            $caption = $Matches[1].Trim()
        }

        # Find all steps and forks by their inner content (no nested divs in these)
        $cells = @()

        # Match steps: title + detail divs inside the step
        $stepMatches = [regex]::Matches($flowBlock, '(?s)<div class="arch-flow-step[^"]*">\s*<div class="arch-flow-step-title">(.*?)</div>\s*<div class="arch-flow-step-detail">(.*?)</div>\s*</div>')
        # Match forks: collect position so we can interleave with steps in order
        $forkMatches = [regex]::Matches($flowBlock, '(?s)<div class="arch-flow-fork">')

        # Build ordered list of elements by their position in the source
        $allElements = @()
        foreach ($sm in $stepMatches) {
            $allElements += [PSCustomObject]@{ Type = 'step'; Index = $sm.Index; Title = $sm.Groups[1].Value.Trim(); Detail = $sm.Groups[2].Value.Trim() }
        }
        foreach ($fm in $forkMatches) {
            # Extract fork content: from the fork div opening to find its branches
            $forkStart = $fm.Index
            $forkContent = $flowBlock.Substring($forkStart, [Math]::Min(500, $flowBlock.Length - $forkStart))
            $forkLabel = ''
            if ($forkContent -match '<div class="arch-flow-fork-label">(.*?)</div>') {
                $forkLabel = $Matches[1].Trim()
            }
            $branches = [regex]::Matches($forkContent, '<div class="arch-flow-branch[^"]*">(.*?)</div>')
            $branchLabels = @($branches | ForEach-Object { $_.Groups[1].Value.Trim() })
            $allElements += [PSCustomObject]@{ Type = 'fork'; Index = $forkStart; ForkLabel = $forkLabel; Branches = $branchLabels }
        }
        $allElements = $allElements | Sort-Object Index

        foreach ($el in $allElements) {
            if ($el.Type -eq 'step') {
                $detail = $el.Detail -replace '<br\s*/?>', ' ' -replace '&rarr;', '&#8594;' -replace '&amp;', '&#38;'
                $cellText = "<strong>$($el.Title)</strong>"
                if ($detail) { $cellText += "<br/>$detail" }
                $cells += "<td>$cellText</td>"
            }
            elseif ($el.Type -eq 'fork') {
                $label = if ($el.ForkLabel) { "<strong>$($el.ForkLabel)</strong><br/>" } else { '' }
                $branchList = $el.Branches -join ' / '
                $cells += "<td>$label$branchList</td>"
            }
        }

        # Build table with arrow cells between steps
        $rowCells = @()
        for ($i = 0; $i -lt $cells.Count; $i++) {
            $rowCells += $cells[$i]
            if ($i -lt $cells.Count - 1) { $rowCells += '<td>&#8594;</td>' }
        }
        $table = "<table><tbody><tr>$($rowCells -join '')</tr></tbody></table>"
        if ($caption) { $table += "<p><em>$caption</em></p>" }

        $body = $body.Substring(0, $startIdx) + $table + $body.Substring($endIdx)
    }

    # -- Now run the standard narrative converter phases --

    # (Phase 1-4: remove non-Confluence elements, convert divs, strip remaining, cleanup)

    # PHASE 1: Remove non-Confluence elements
    $body = $body -replace '(?s)<div class="page-header">\s*<h1>.*?</h1>\s*<div class="subtitle">.*?</div>\s*</div>', ''
    $body = $body -replace '(?s)<div class="doc-nav">.*?</div>', ''
    $body = $body -replace '(?s)<div class="doc-footer">\s*(?:<p[^>]*>.*?</p>\s*)*</div>', ''
    $body = $body -replace '(?s)<div style="text-align: center;">\s*<a[^>]*class="ref-link"[^>]*>.*?</a>\s*</div>', ''
    $body = $body -replace '(?s)<!--.*?-->', ''
    $body = $body -replace '(?s)<script[^>]*>.*?</script>', ''

    # PHASE 2: Convert remaining special divs (context-bar, callouts, flows)
    $body = [regex]::Replace($body, '(?s)<div class="context-bar">\s*(.*?)\s*</div>', {
        param($m)
        $content = $m.Groups[1].Value.Trim()
        "<ac:structured-macro ac:name=`"info`"><ac:parameter ac:name=`"title`">Overview</ac:parameter><ac:rich-text-body>`n$content`n</ac:rich-text-body></ac:structured-macro>"
    })

    $body = [regex]::Replace($body, '(?s)<div class="callout info">\s*(.*?)\s*</div>', {
        param($m)
        "<ac:structured-macro ac:name=`"info`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
    })

    $body = [regex]::Replace($body, '(?s)<div class="callout tip">\s*(.*?)\s*</div>', {
        param($m)
        "<ac:structured-macro ac:name=`"tip`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
    })

    $body = [regex]::Replace($body, '(?s)<div class="callout warning">\s*(.*?)\s*</div>', {
        param($m)
        "<ac:structured-macro ac:name=`"warning`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
    })

    $body = [regex]::Replace($body, '(?s)<div class="callout story">\s*(.*?)\s*</div>', {
        param($m)
        "<ac:structured-macro ac:name=`"note`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
    })

    # Expand cards -> Confluence expand macros (with separator lines)
    $body = [regex]::Replace($body, '(?s)<div class="expand-card">\s*<div class="expand-card-title">(.*?)</div>\s*<div class="expand-card-body">(.*?)</div>\s*</div>', {
        param($m)
        $title = $m.Groups[1].Value.Trim()
        $cardBody = $m.Groups[2].Value.Trim()
        $escaped = [System.Security.SecurityElement]::Escape($title)
        "<hr/><ac:structured-macro ac:name=`"expand`"><ac:parameter ac:name=`"title`">$escaped</ac:parameter><ac:rich-text-body>`n$cardBody`n</ac:rich-text-body></ac:structured-macro><hr/>"
    })

    # PHASE 2.5: XHTML compliance
    $body = $body -replace '<br\s*(?!/)>', '<br/>'
    $body = $body -replace '<span class="arch-arrow"[^>]*>.*?</span>', ''
    $body = $body -replace '<span class="pattern-arrow"[^>]*>.*?</span>', ''
    $body = $body -replace '<span class="fac"[^>]*>.*?</span>', ''

    # PHASE 3: Strip ALL remaining div tags
    $body = $body -replace '<div[^>]*>', ''
    $body = $body -replace '</div>', ''

    # PHASE 4: Clean up tables, links, entities
    $body = $body -replace 'class="info-table"', ''
    $body = $body -replace 'class="tech-table"', ''
    $body = $body -replace '<span class="color-sample"[^>]*></span>', ''
    $body = $body -replace '<a[^>]*class="ref-inline"[^>]*><code>([^<]*)</code></a>', '<code>$1</code>'
    $body = $body -replace '<p class="muted">(.*?)</p>', '<p><em>$1</em></p>'
    $body = $body -replace '<br\s*(?!/)>', '<br/>'

    # Entity cleanup
    $body = $body -replace '&mdash;', '&#8212;'
    $body = $body -replace '&rarr;', '&#8594;'
    $body = $body -replace '&larr;', '&#8592;'
    $body = $body -replace '&nbsp;', '&#160;'
    $body = $body -replace '&hellip;', '&#8230;'
    $body = $body -replace '&ndash;', '&#8211;'

    # XHTML compliance - escape bare ampersands in URLs (e.g., mailto query strings)
    $body = [regex]::Replace($body, '&(?!amp;|#\d+;|#x[\da-fA-F]+;|\w+;)', '&amp;')

    # Strip inline styles with CSS variables
    $body = $body -replace 'style="color: var\([^)]*\);[^"]*"', ''
    $body = $body -replace 'style="[^"]*var\([^)]*\)[^"]*"', ''

    # Whitespace cleanup
    $body = $body -replace '(?m)^\s*$\n', ''
    $body = $body.Trim()

    return $body
}

# Wraps a title and body in a Confluence expand macro.
function ConvertTo-ConfluenceExpand {
    param([string]$Title, [string]$Body)
    @"
<ac:structured-macro ac:name="expand">
<ac:parameter ac:name="title">$([System.Security.SecurityElement]::Escape($Title))</ac:parameter>
<ac:rich-text-body>
$Body
</ac:rich-text-body>
</ac:structured-macro>
"@
}

# Emits a Confluence table macro from header and row data.
function ConvertTo-ConfluenceTable {
    param([string[]]$Headers, [string[][]]$Rows)
    $h = '<table><colgroup>' + ('<col/>' * $Headers.Count) + '</colgroup><tbody>'
    $headerCells = $Headers | ForEach-Object { "<th>$([System.Security.SecurityElement]::Escape($_))</th>" }
    $h += '<tr>' + ($headerCells -join '') + '</tr>'
    foreach ($row in $Rows) {
        $rowCells = $row | ForEach-Object { "<td>$([System.Security.SecurityElement]::Escape($_))</td>" }
        $h += '<tr>' + ($rowCells -join '') + '</tr>'
    }
    $h += '</tbody></table>'
    return $h
}

