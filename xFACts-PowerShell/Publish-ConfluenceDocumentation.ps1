# Renders one schema object (table, proc, function, view, trigger) as Storage Format.
function Convert-JsonObjectToStorageFormat {
    param($Obj, [string]$TypeLabel)

    $esc = { param($t) [System.Security.SecurityElement]::Escape($t) }
    $name = $Obj.name
    $body = ''

    # Module / Category line
    if ($Obj.module -or $Obj.category) {
        $body += "<p><strong>Module:</strong> $(& $esc $Obj.module) &#160;|&#160; <strong>Category:</strong> $(& $esc $Obj.category)</p>"
    }

    # Description
    if ($Obj.description) {
        $body += "<p>$(& $esc $Obj.description)</p>"
    }

    # Data Flow (always visible)
    if ($Obj.dataFlow) {
        $body += ConvertTo-ConfluenceInfoPanel -Title 'Data Flow' -Body "<p>$(& $esc $Obj.dataFlow)</p>"
    }

    # Collect inner expand sections - joined with <hr/> for visual separation
    $sections = @()

    # Fields (tables)
    if ($Obj.columns -and $Obj.columns.Count -gt 0) {
        $rows = @()
        foreach ($col in $Obj.columns) {
            $dtype = if ($col.dataType) { $col.dataType.ToUpper() } else { '' }
            if ($col.length) { $dtype += "($($col.length))" }
            $nullable = if ($col.nullable) { 'Yes' } else { 'No' }
            $dv = if ($col.default) { $col.default } else { '' }
            while ($dv.StartsWith('(') -and $dv.EndsWith(')')) { $dv = $dv.Substring(1, $dv.Length - 2) }
            $display = if ($col.identity) { 'IDENTITY' } elseif ($dv) { $dv } else { [char]0x2014 }
            $desc = if ($col.description) { $col.description } else { '' }
            $rows += ,@($col.name, $dtype, $nullable, [string]$display, $desc)
        }
        $fieldHtml = ConvertTo-ConfluenceTable -Headers @('Column','Type','Nullable','Default','Description') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Fields ($($Obj.columns.Count))" -Body $fieldHtml
    }

    # Parameters (procs/functions)
    if ($Obj.parameters -and $Obj.parameters.Count -gt 0) {
        $rows = @()
        foreach ($p in $Obj.parameters) {
            $dtype = if ($p.dataType) { $p.dataType.ToUpper() } else { '' }
            if ($p.length) { $dtype += "($($p.length))" }
            $opt = if ($p.isOptional) { 'Optional' } else { 'Required' }
            $desc = if ($p.description) { $p.description } else { '' }
            $rows += ,@($p.name, $dtype, $opt, $desc)
        }
        $paramHtml = ConvertTo-ConfluenceTable -Headers @('Parameter','Type','Required','Description') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Parameters ($($Obj.parameters.Count))" -Body $paramHtml
    }

    # Indexes
    if ($Obj.indexes -and $Obj.indexes.Count -gt 0) {
        $rows = @()
        foreach ($idx in $Obj.indexes) {
            $cols = $(if ($idx.columns) { $idx.columns } else { @() }) -join ', '
            $inc = $(if ($idx.includes) { $idx.includes } else { @() }) -join ', '
            $rows += ,@($idx.name, $(if ($idx.type) { $idx.type } else { '' }), $cols, $inc)
        }
        $idxHtml = ConvertTo-ConfluenceTable -Headers @('Index','Type','Columns','Includes') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Indexes ($($Obj.indexes.Count))" -Body $idxHtml
    }

    # Check Constraints
    if ($Obj.checkConstraints -and $Obj.checkConstraints.Count -gt 0) {
        $rows = @()
        foreach ($ck in $Obj.checkConstraints) {
            $rows += ,@($ck.name, $(if ($ck.definition) { $ck.definition } else { '' }))
        }
        $ckHtml = ConvertTo-ConfluenceTable -Headers @('Constraint','Definition') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Check Constraints ($($Obj.checkConstraints.Count))" -Body $ckHtml
    }

    # Foreign Keys
    if ($Obj.foreignKeys -and $Obj.foreignKeys.Count -gt 0) {
        $rows = @()
        foreach ($fk in $Obj.foreignKeys) {
            $colMap = ($fk.columnMappings | ForEach-Object { "$($_.column) -> $($_.referencedColumn)" }) -join ', '
            $rows += ,@($fk.name, $(if ($fk.referencedTable) { $fk.referencedTable } else { '' }), $colMap)
        }
        $fkHtml = ConvertTo-ConfluenceTable -Headers @('Foreign Key','References','Mapping') -Rows $rows
        $sections += ConvertTo-ConfluenceExpand -Title "Foreign Keys ($($Obj.foreignKeys.Count))" -Body $fkHtml
    }

    # Design Notes
    if ($Obj.designNotes -and $Obj.designNotes.Count -gt 0) {
        $dnHtml = ''
        foreach ($note in $Obj.designNotes) {
            $dnHtml += "<p><strong>$(& $esc $note.topic)</strong></p>"
            if ($note.summary) { $dnHtml += "<p><em>$(& $esc $note.summary)</em></p>" }
            $dnHtml += "<p>$(& $esc $note.note)</p>"
        }
        $sections += ConvertTo-ConfluenceExpand -Title "Design Notes ($($Obj.designNotes.Count))" -Body $dnHtml
    }

    # Status Values
    if ($Obj.statusValues -and $Obj.statusValues.Count -gt 0) {
        $byCol = @{}
        foreach ($sv in $Obj.statusValues) {
            $col = if ($sv.column) { $sv.column } else { 'status' }
            if (-not $byCol.ContainsKey($col)) { $byCol[$col] = @() }
            $byCol[$col] += $sv
        }
        $svHtml = ''
        foreach ($col in $byCol.Keys) {
            $svHtml += "<p><strong>Column: $(& $esc $col)</strong></p>"
            $rows = @()
            foreach ($v in $byCol[$col]) {
                $rows += ,@($(if ($v.value) { $v.value } else { '' }), $(if ($v.meaning) { $v.meaning } else { '' }))
            }
            $svHtml += ConvertTo-ConfluenceTable -Headers @('Value','Meaning') -Rows $rows
        }
        $sections += ConvertTo-ConfluenceExpand -Title "Status Values ($($Obj.statusValues.Count))" -Body $svHtml
    }

    # Common Queries
    if ($Obj.queries -and $Obj.queries.Count -gt 0) {
        $qHtml = ''
        foreach ($q in $Obj.queries) {
            $qHtml += "<p><strong>$(& $esc $q.name)</strong></p>"
            if ($q.description) { $qHtml += "<p><em>$(& $esc $q.description)</em></p>" }
            $qHtml += ConvertTo-ConfluenceCodeBlock -Code $q.sql
        }
        $sections += ConvertTo-ConfluenceExpand -Title "Common Queries ($($Obj.queries.Count))" -Body $qHtml
    }

    # Relationship Notes
    if ($Obj.relationshipNotes -and $Obj.relationshipNotes.Count -gt 0) {
        $relHtml = ''
        foreach ($rel in $Obj.relationshipNotes) {
            $relHtml += "<p><strong>$(& $esc $rel.relatedObject)</strong></p>"
            $relHtml += "<p>$(& $esc $rel.note)</p>"
        }
        $sections += ConvertTo-ConfluenceExpand -Title "Relationships ($($Obj.relationshipNotes.Count))" -Body $relHtml
    }

    # Join sections with horizontal rule separators
    $body += $sections -join '<hr/>'

    return ConvertTo-ConfluenceExpand -Title "$name ($TypeLabel)" -Body $body
}

