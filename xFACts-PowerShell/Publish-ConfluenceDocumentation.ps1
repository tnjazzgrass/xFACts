            })
            $sectionContent = [regex]::Replace($sectionContent, '(?s)<div class="callout warning">\s*(.*?)\s*</div>', {
                param($m)
                "<ac:structured-macro ac:name=`"warning`"><ac:rich-text-body>`n$($m.Groups[1].Value.Trim())`n</ac:rich-text-body></ac:structured-macro>"
            })

            # Strip remaining div tags (structural wrappers only at this point)
            $sectionContent = $sectionContent -replace '<div[^>]*>', ''
            $sectionContent = $sectionContent -replace '</div>', ''

            # XHTML compliance - self-close bare <br> tags
            $sectionContent = $sectionContent -replace '<br\s*(?!/)>', '<br/>'

            # Entity cleanup
            $sectionContent = $sectionContent -replace '&mdash;', '&#8212;'
            $sectionContent = $sectionContent -replace '&rsquo;', '&#8217;'
            $sectionContent = $sectionContent -replace '&ldquo;', '&#8220;'
            $sectionContent = $sectionContent -replace '&rdquo;', '&#8221;'
            $sectionContent = $sectionContent -replace '&rarr;', '&#8594;'
            $sectionContent = $sectionContent -replace '&larr;', '&#8592;'
            $sectionContent = $sectionContent -replace '&nbsp;', '&#160;'
            $sectionContent = $sectionContent -replace '&hellip;', '&#8230;'
            $sectionContent = $sectionContent -replace '&ndash;', '&#8211;'
            $sectionContent = $sectionContent -replace '&bull;', '&#8226;'
            $sectionContent = $sectionContent -replace '&middot;', '&#183;'
            $sectionContent = $sectionContent -replace '&times;', '&#215;'
            $sectionContent = [regex]::Replace($sectionContent, '&(?!amp;|#\d+;|#x[\da-fA-F]+;|\w+;)', '&amp;')

            # Strip inline styles with CSS variables
            $sectionContent = $sectionContent -replace 'style="color: var\([^)]*\);[^"]*"', ''
            $sectionContent = $sectionContent -replace 'style="[^"]*var\([^)]*\)[^"]*"', ''

            # Clean whitespace
            $sectionContent = $sectionContent -replace '(?m)^\s*$\n', ''
            $sectionContent = $sectionContent.Trim()

            # Build expand macro (following ref page pattern)
            $escapedTitle = [System.Security.SecurityElement]::Escape($sectionTitle)
            $body += "`n<hr/><ac:structured-macro ac:name=`"expand`"><ac:parameter ac:name=`"title`">$escapedTitle</ac:parameter><ac:rich-text-body>`n$sectionContent`n</ac:rich-text-body></ac:structured-macro>"
        }

        # Trailing separator after last expand
        $body += "`n<hr/>"
    }

    # Whitespace cleanup
    $body = $body -replace '(?m)^\s*$\n', ''
    $body = $body.Trim()

    return $body
}

