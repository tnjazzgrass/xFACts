# Establishes an authenticated Confluence Server session via form login.
function Connect-Confluence {
    param(
        [string]$BaseUrl,
        [PSCredential]$Credential
    )

    $session = New-Object Microsoft.PowerShell.Commands.WebRequestSession

    $loginBody = @{
        os_username    = $Credential.UserName
        os_password    = $Credential.GetNetworkCredential().Password
        os_destination = '/rest/api/content?limit=1'
        login          = 'Log In'
    }

    Write-Console "  Authenticating to Confluence..." 'Cyan'
    $null = Invoke-WebRequest -Uri "$BaseUrl/dologin.action" -Method Post -Body $loginBody -WebSession $session -MaximumRedirection 0 -UseBasicParsing -ErrorAction SilentlyContinue

    # Verify session works
    try {
        $test = Invoke-RestMethod -Uri ("$BaseUrl/rest/api/content?limit=1" + "&spaceKey=$SpaceKey") -WebSession $session -UseBasicParsing -ErrorAction Stop
        if ($null -eq $test.results -or $test.size -eq 0) {
            # Could be empty but valid - try space lookup
            $spaceTest = Invoke-RestMethod -Uri "$BaseUrl/rest/api/space/$SpaceKey" -WebSession $session -UseBasicParsing -ErrorAction Stop
            Write-Console "  Authenticated. Space: $($spaceTest.name)" 'Green'
        } else {
            Write-Console "  Authenticated. Space accessible." 'Green'
        }
    }
    catch {
        Write-Warning "Authentication may have failed. Continuing - errors will surface on page operations."
    }

    return $session
}

# Looks up a Confluence page by space and title, returning it or null.
function Get-ConfluencePage {
    param(
        [string]$BaseUrl,
        $Session,
        [string]$SpaceKey,
        [string]$Title
    )

    $encodedTitle = [uri]::EscapeDataString($Title)
    $url = "$BaseUrl/rest/api/content?spaceKey=$SpaceKey" + "&title=$encodedTitle" + "&expand=version,ancestors"

    try {
        $result = Invoke-RestMethod -Uri $url -WebSession $Session -UseBasicParsing -ErrorAction Stop
        if ($result.results.Count -gt 0) {
            return $result.results[0]
        }
    }
    catch {
        Write-Warning "Error looking up page '$Title': $($_.Exception.Message)"
    }

    return $null
}

# Creates a new Confluence page under an optional parent.
function New-ConfluencePage {
    param(
        [string]$BaseUrl,
        $Session,
        [string]$SpaceKey,
        [string]$Title,
        [string]$StorageBody,
        [string]$ParentPageId
    )

    $pageData = @{
        type  = 'page'
        title = $Title
        space = @{ key = $SpaceKey }
        body  = @{
            storage = @{
                value          = $StorageBody
                representation = 'storage'
            }
        }
    }

    if ($ParentPageId) {
        $pageData.ancestors = @( @{ id = $ParentPageId } )
    }

    $json = $pageData | ConvertTo-Json -Depth 10
    $bytes = [System.Text.Encoding]::UTF8.GetBytes($json)

    try {
        $result = Invoke-RestMethod -Uri "$BaseUrl/rest/api/content" -Method Post -Body $bytes -ContentType 'application/json; charset=utf-8' -WebSession $Session -UseBasicParsing -ErrorAction Stop
        return $result
    }
    catch {
        $errMsg = $_.Exception.Message
        if ($_.Exception.Response) {
            $reader = New-Object System.IO.StreamReader($_.Exception.Response.GetResponseStream())
            $errMsg += " - " + $reader.ReadToEnd()
        }
        Write-Error "Failed to create page '$Title': $errMsg"
        return $null
    }
}

# Updates an existing Confluence page to a new body and version.
function Update-ConfluencePage {
    param(
        [string]$BaseUrl,
        $Session,
        [string]$PageId,
        [string]$Title,
        [string]$SpaceKey,
        [int]$CurrentVersion,
        [string]$StorageBody,
        [string]$ParentPageId
    )

    $pageData = @{
        id      = $PageId
        type    = 'page'
        title   = $Title
        space   = @{ key = $SpaceKey }
        version = @{ number = ($CurrentVersion + 1) }
        body    = @{
            storage = @{
                value          = $StorageBody
                representation = 'storage'
            }
        }
    }

    # Always set ancestors to ensure correct parent - handles re-parenting
    # when the page tree is recreated with new IDs
    if ($ParentPageId) {
        $pageData.ancestors = @( @{ id = $ParentPageId } )
    }

    $json = $pageData | ConvertTo-Json -Depth 10
    $bytes = [System.Text.Encoding]::UTF8.GetBytes($json)

    try {
        $result = Invoke-RestMethod -Uri "$BaseUrl/rest/api/content/$PageId" -Method Put -Body $bytes -ContentType 'application/json; charset=utf-8' -WebSession $Session -UseBasicParsing -ErrorAction Stop
        return $result
    }
    catch {
        $errMsg = $_.Exception.Message
        if ($_.Exception.Response) {
            $reader = New-Object System.IO.StreamReader($_.Exception.Response.GetResponseStream())
            $errMsg += " - " + $reader.ReadToEnd()
        }
        Write-Error "Failed to update page '$Title': $errMsg"
        return $null
    }
}

# Applies edit restrictions to a page while leaving view open.
function Set-ConfluencePageRestrictions {
    param(
        [string]$BaseUrl,
        $Session,
        [string]$PageId,
        [string[]]$Editors
    )

    # Confluence Server 7.x: Set edit restriction per user via experimental API
    # First try the standard approach, fall back to individual adds
    foreach ($editor in $Editors) {
        $restrictionBody = @{
            operation = 'update'
            restrictions = @{
                user = @(
                    @{
                        type = 'known'
                        username = $editor
                    }
                )
            }
        }

        $json = ConvertTo-Json -InputObject @($restrictionBody) -Depth 10
        $bytes = [System.Text.Encoding]::UTF8.GetBytes($json)

        try {
            # Try the experimental bulk endpoint first
            $url = "$BaseUrl/rest/experimental/content/$PageId/restriction"
            $null = Invoke-RestMethod -Uri $url -Method Put -Body $bytes -ContentType 'application/json; charset=utf-8' -WebSession $Session -UseBasicParsing -ErrorAction Stop
            Write-Console "  Restrictions set (editors: $($Editors -join ', '))" 'Gray'
            return
        }
        catch {
            # Fall back: try adding individual restriction
            try {
                $url = "$BaseUrl/rest/api/content/$PageId/restriction/byOperation/update/user" + "?userName=$editor"
                $null = Invoke-RestMethod -Uri $url -Method Put -ContentType 'application/json; charset=utf-8' -WebSession $Session -UseBasicParsing -ErrorAction Stop
                Write-Console "  Edit restriction added for: $editor" 'Gray'
            }
            catch {
                Write-Warning "  Could not set restrictions on page $PageId for $editor - $($_.Exception.Message)"
                Write-Warning "  You may need to set edit restrictions manually for this page."
                return
            }
        }
    }
}

# Creates or updates a page by title and applies edit restrictions.
function Publish-ConfluencePage {
    param(
        [string]$BaseUrl,
        $Session,
        [string]$SpaceKey,
        [string]$Title,
        [string]$StorageBody,
        [string]$ParentPageId,
        [bool]$Execute,
        [string[]]$Editors
    )

    $existing = Get-ConfluencePage -BaseUrl $BaseUrl -Session $Session -SpaceKey $SpaceKey -Title $Title

    if ($existing) {
        Write-Console "  Page exists (ID: $($existing.id), v$($existing.version.number)). Updating..." 'Yellow'
        if ($Execute) {
            $result = Update-ConfluencePage -BaseUrl $BaseUrl -Session $Session -PageId $existing.id -Title $Title -SpaceKey $SpaceKey -CurrentVersion $existing.version.number -StorageBody $StorageBody -ParentPageId $ParentPageId
            if ($result) {
                Write-Console "  Updated to v$($result.version.number)" 'Green'
            }
            if ($Editors) {
                Set-ConfluencePageRestrictions -BaseUrl $BaseUrl -Session $Session -PageId $existing.id -Editors $Editors
            }
            return $existing.id
        } else {
            Write-Console "  [PREVIEW] Would update to v$($existing.version.number + 1)" 'DarkYellow'
            return $existing.id
        }
    } else {
        Write-Console "  Page does not exist. Creating..." 'Yellow'
        if ($Execute) {
            $result = New-ConfluencePage -BaseUrl $BaseUrl -Session $Session -SpaceKey $SpaceKey -Title $Title -StorageBody $StorageBody -ParentPageId $ParentPageId
            if ($result) {
                Write-Console "  Created (ID: $($result.id))" 'Green'
                if ($Editors) {
                    Set-ConfluencePageRestrictions -BaseUrl $BaseUrl -Session $Session -PageId $result.id -Editors $Editors
                }
                return $result.id
            }
            return $null
        } else {
            Write-Console "  [PREVIEW] Would create as child of $ParentPageId" 'DarkYellow'
            return 'preview-id'
        }
    }
}

<# ============================================================================
   FUNCTIONS: MARKDOWN CONVERSION
   ----------------------------------------------------------------------------
   Converters that render the same documentation HTML and reference JSON into
   markdown for the full and reference-only exports.
   Prefix: (none)
   ============================================================================ #>

# Converts a narrative documentation HTML page into markdown.
function Convert-HtmlToMarkdown {
    param([string]$HtmlContent)

    $body = $HtmlContent

    # Extract page-wrapper content
    if ($body -match '(?s)<div class="page-wrapper">(.*)</div>\s*<script') {
        $body = $Matches[1]
    }

    # Extract title
    $title = ''
    if ($body -match '<h1>(.*?)</h1>') {
        $title = $Matches[1] -replace '<[^>]+>', ''
        $title = $title -replace '&mdash;', [char]0x2014
        $title = $title -replace '&rsquo;', [char]0x2019
        $title = $title -replace '&amp;', '&'
    }

    # Extract subtitle
    $subtitle = ''
    if ($body -match '<div class="subtitle">(.*?)</div>') {
        $subtitle = $Matches[1]
        $subtitle = $subtitle -replace '&mdash;', [char]0x2014
        $subtitle = $subtitle -replace '&rsquo;', [char]0x2019
        $subtitle = $subtitle -replace '&ldquo;', [char]0x201C
        $subtitle = $subtitle -replace '&rdquo;', [char]0x201D
        $subtitle = $subtitle -replace '&amp;', '&'
    }

    # Remove non-content elements
    $body = $body -replace '(?s)<div class="page-header">.*?</div>\s*</div>', ''
    $body = $body -replace '(?s)<div class="doc-nav">.*?</div>', ''
    $body = $body -replace '(?s)<div class="doc-footer">\s*(?:<p[^>]*>.*?</p>\s*)*</div>', ''
    $body = $body -replace '(?s)<div style="text-align: center;">\s*<a[^>]*class="ref-link"[^>]*>.*?</a>\s*</div>', ''
    $body = $body -replace '(?s)<!--.*?-->', ''
    $body = $body -replace '(?s)<script[^>]*>.*?</script>', ''

    # Convert headings
    $body = $body -replace '<h2>(.*?)</h2>', "`n## `$1`n"
    $body = $body -replace '<h3>(.*?)</h3>', "`n### `$1`n"
    $body = $body -replace '<h4>(.*?)</h4>', "`n#### `$1`n"

    # Convert inline formatting
    $body = $body -replace '<strong>(.*?)</strong>', '**$1**'
    $body = $body -replace '<em>(.*?)</em>', '*$1*'
    $body = $body -replace '<code>(.*?)</code>', '`$1`'
    $body = $body -replace '<a[^>]*class="ref-inline"[^>]*><code>([^<]*)</code></a>', '`$1`'
    $body = $body -replace '<a[^>]*href="([^"]*)"[^>]*>(.*?)</a>', '$2'
    $body = $body -replace '<br\s*/?>', "`n"

    # Convert flow-diagram blocks to markdown pipe tables
    while ($body.Contains('<div class="flow-diagram">')) {
        $fdStart = $body.IndexOf('<div class="flow-diagram">')
        # Find matching close by depth counting
        $depth = 0; $si = $fdStart; $fdEnd = -1
        while ($si -lt $body.Length) {
            $no = $body.IndexOf('<div', $si + 1)
            $nc = $body.IndexOf('</div>', $si + 1)
            if ($nc -eq -1) { break }
            if ($si -eq $fdStart) { $depth = 1; $si = $fdStart + 1; continue }
            if ($no -ne -1 -and $no -lt $nc) { $depth++; $si = $no + 1 }
            else { $depth--; if ($depth -eq 0) { $fdEnd = $nc + 6; break }; $si = $nc + 1 }
        }
        if ($fdEnd -eq -1) { break }

        $fdBlock = $body.Substring($fdStart, $fdEnd - $fdStart)
        $caption = ''
        if ($fdBlock -match '<div class="flow-caption">(.*?)</div>') { $caption = $Matches[1].Trim() }

        $stepMatches = [regex]::Matches($fdBlock, '(?s)<div class="flow-step[^"]*">(.*?)</div>')
        $cells = @()
        foreach ($sm in $stepMatches) {
            $text = ($sm.Groups[1].Value -replace '<br\s*/?>', ' ' -replace '<[^>]+>', '').Trim()
            $cells += $text
        }

        if ($cells.Count -gt 0) {
            $headerRow = '| ' + ($cells -join ' | ') + ' |'
            $sepRow = '| ' + (($cells | ForEach-Object { '---' }) -join ' | ') + ' |'
            $arrowRow = '| ' + (($cells | ForEach-Object { [char]0x2192 }) -join ' | ') + ' |'
            $mdTable = "$headerRow`n$sepRow"
            if ($caption) { $mdTable += "`n`n*$caption*" }
            $mdTable += "`n"
        } else {
            $mdTable = ''
        }

        $body = $body.Substring(0, $fdStart) + $mdTable + $body.Substring($fdEnd)
    }

    # Convert tables to markdown pipe tables
    $body = [regex]::Replace($body, '(?s)<table[^>]*>(.*?)</table>', {
        param($m)
        $tableHtml = $m.Groups[1].Value
        $rows = [regex]::Matches($tableHtml, '(?s)<tr[^>]*>(.*?)</tr>')
        $mdRows = @()
        $rowIndex = 0
        foreach ($row in $rows) {
            $cells = [regex]::Matches($row.Groups[1].Value, '(?s)<t[hd][^>]*>(.*?)</t[hd]>')
            $cleanCells = @()
            foreach ($cell in $cells) {
                $c = $cell.Groups[1].Value -replace '<[^>]+>', ''
                $c = $c.Trim() -replace '\|', '\|'
                $cleanCells += $c
            }
            $mdRows += '| ' + ($cleanCells -join ' | ') + ' |'
            if ($rowIndex -eq 0) {
                $mdRows += '| ' + (($cleanCells | ForEach-Object { '---' }) -join ' | ') + ' |'
            }
            $rowIndex++
        }
        ($mdRows -join "`n") + "`n"
    })

    # Strip all remaining HTML tags
    $body = $body -replace '<[^>]+>', ''

    # Entity decode - named entities
    $body = $body -replace '&mdash;', [char]0x2014
    $body = $body -replace '&ndash;', [char]0x2013
    $body = $body -replace '&rsquo;', [char]0x2019
    $body = $body -replace '&ldquo;', [char]0x201C
    $body = $body -replace '&rdquo;', [char]0x201D
    $body = $body -replace '&apos;', "'"
    $body = $body -replace '&rarr;', [char]0x2192
    $body = $body -replace '&larr;', [char]0x2190
    $body = $body -replace '&uarr;', [char]0x2191
    $body = $body -replace '&darr;', [char]0x2193
    $body = $body -replace '&nbsp;', ' '
    $body = $body -replace '&hellip;', [char]0x2026
    $body = $body -replace '&bull;', [char]0x2022
    $body = $body -replace '&middot;', [char]0x00B7
    $body = $body -replace '&times;', [char]0x00D7
    $body = $body -replace '&ge;', [char]0x2265
    $body = $body -replace '&eacute;', [char]0x00E9
    $body = $body -replace '&amp;', '&'
    $body = $body -replace '&lt;', '<'
    $body = $body -replace '&gt;', '>'
    # Entity decode - numeric entities
    $body = $body -replace '&#8212;', [char]0x2014
    $body = $body -replace '&#8211;', [char]0x2013
    $body = $body -replace '&#8217;', [char]0x2019
    $body = $body -replace '&#8220;', [char]0x201C
    $body = $body -replace '&#8221;', [char]0x201D
    $body = $body -replace '&#8594;', [char]0x2192
    $body = $body -replace '&#8592;', [char]0x2190
    $body = $body -replace '&#160;', ' '
    $body = $body -replace '&#8230;', [char]0x2026
    $body = $body -replace '&#8226;', [char]0x2022

    # Whitespace cleanup - strip leading spaces/tabs from each line (HTML indentation artifacts)
    $body = $body -replace '(?m)^[ \t]+', ''
    # Collapse 3+ consecutive newlines to 2
    $body = $body -replace '(?m)\n{3,}', "`n`n"
    $body = $body.Trim()

    # Build final markdown
    $md = "# $title`n`n"
    if ($subtitle) { $md += "*$subtitle*`n`n" }
    $md += $body

    return $md
}

# Converts an architecture HTML page into markdown, rendering ERDs and panels as markdown.
function Convert-ArchHtmlToMarkdown {
    param(
        [string]$HtmlContent,
        [hashtable]$JsonDataCache
    )

    $body = $HtmlContent

    # Extract page-wrapper content
    if ($body -match '(?s)<div class="page-wrapper">(.*)</div>\s*<script') {
        $body = $Matches[1]
    }

    # Extract title
    $title = ''
    if ($body -match '<h1>(.*?)</h1>') {
        $title = $Matches[1] -replace '<[^>]+>', ''
        $title = $title -replace '&mdash;', [char]0x2014
        $title = $title -replace '&rsquo;', [char]0x2019
        $title = $title -replace '&amp;', '&'
    }

    # Strip section-nav (match known structure to avoid overshooting into content)
    $body = $body -replace '(?s)<div class="section-nav">\s*<div class="section-nav-top"></div>\s*<div class="section-nav-links">.*?</div>\s*</div>', ''

    # Convert ERD divs to PlantUML code blocks
    $body = [regex]::Replace($body, '<div class="erd-root"\s+data-schema="([^"]+)"(?:\s+data-category="([^"]*)")?\s*>\s*</div>', {
        param($m)
        $schema = $m.Groups[1].Value
        $category = $m.Groups[2].Value

        if ($JsonDataCache -and $JsonDataCache.ContainsKey($schema)) {
            $uml = Convert-JsonToPlantUmlErd -JsonData $JsonDataCache[$schema] -SchemaName $schema -CategoryFilter $category
            if ($uml) {
                return "``````plantuml`n$uml`n```````n"
            }
        }

        $label = $schema
        if ($category) { $label += " - $category" }
        return "*ERD: $label (see Control Center for interactive diagram)*`n"
    })

    # Convert tech-tip divs to blockquotes
    $body = [regex]::Replace($body, '(?s)<div class="tech-tip">\s*(.*?)\s*</div>', {
        param($m)
        $content = $m.Groups[1].Value.Trim()
        $content = $content -replace '<strong>(.*?)</strong>', '**$1**'
        $content = $content -replace '<code>(.*?)</code>', '`$1`'
        $content = $content -replace '<em>(.*?)</em>', '*$1*'
        $content = $content -replace '<[^>]+>', ''
        $lines = $content -split "`n" | ForEach-Object { "> $($_.Trim())" }
        ($lines -join "`n") + "`n"
    })

    # Convert diagram placeholders to italicized note
    $body = [regex]::Replace($body, '(?s)<div class="diagram-placeholder">\s*<div class="placeholder-label">(.*?)</div>\s*<div class="placeholder-desc">(.*?)</div>\s*</div>', {
        param($m)
        "*$($m.Groups[1].Value.Trim()): $($m.Groups[2].Value.Trim())*`n"
    })

    # Convert arch-flow blocks to markdown pipe tables (steps + forks + captions)
    while ($body.Contains('<div class="arch-flow">')) {
        $afStart = $body.IndexOf('<div class="arch-flow">')
        # Find matching close by depth counting
        $depth = 0; $si = $afStart; $afEnd = -1
        while ($si -lt $body.Length) {
            $no = $body.IndexOf('<div', $si + 1)
            $nc = $body.IndexOf('</div>', $si + 1)
            if ($nc -eq -1) { break }
            if ($si -eq $afStart) { $depth = 1; $si = $afStart + 1; continue }
            if ($no -ne -1 -and $no -lt $nc) { $depth++; $si = $no + 1 }
            else { $depth--; if ($depth -eq 0) { $afEnd = $nc + 6; break }; $si = $nc + 1 }
        }
        if ($afEnd -eq -1) { break }

        $afBlock = $body.Substring($afStart, $afEnd - $afStart)

        # Extract caption
        $caption = ''
        if ($afBlock -match '<div class="arch-flow-caption">(.*?)</div>') { $caption = $Matches[1].Trim() }

        # Find steps and forks by position
        $stepMatches = [regex]::Matches($afBlock, '(?s)<div class="arch-flow-step[^"]*">\s*<div class="arch-flow-step-title">(.*?)</div>\s*<div class="arch-flow-step-detail">(.*?)</div>\s*</div>')
        $forkMatches = [regex]::Matches($afBlock, '(?s)<div class="arch-flow-fork">')

        $allElements = @()
        foreach ($sm in $stepMatches) {
            $title = ($sm.Groups[1].Value -replace '<[^>]+>', '').Trim()
            $detail = ($sm.Groups[2].Value -replace '<br\s*/?>', ' ' -replace '<[^>]+>', '').Trim()
            $allElements += [PSCustomObject]@{ Type = 'step'; Index = $sm.Index; Text = "**$title** $detail" }
        }
        foreach ($fm in $forkMatches) {
            $forkContent = $afBlock.Substring($fm.Index, [Math]::Min(500, $afBlock.Length - $fm.Index))
            $forkLabel = ''
            if ($forkContent -match '<div class="arch-flow-fork-label">(.*?)</div>') { $forkLabel = $Matches[1].Trim() }
            $branches = [regex]::Matches($forkContent, '<div class="arch-flow-branch[^"]*">(.*?)</div>')
            $branchLabels = @($branches | ForEach-Object { $_.Groups[1].Value.Trim() })
            $text = if ($forkLabel) { "**$forkLabel**: $($branchLabels -join ' / ')" } else { $branchLabels -join ' / ' }
            $allElements += [PSCustomObject]@{ Type = 'fork'; Index = $fm.Index; Text = $text }
        }
        $allElements = $allElements | Sort-Object Index

        $cells = @($allElements | ForEach-Object { $_.Text })

        if ($cells.Count -gt 0) {
            # Build single-row pipe table with arrow separators
            $headerCells = @()
            for ($i = 0; $i -lt $cells.Count; $i++) {
                $headerCells += $cells[$i]
                if ($i -lt $cells.Count - 1) { $headerCells += [string][char]0x2192 }
            }
            $headerRow = '| ' + ($headerCells -join ' | ') + ' |'
            $sepRow = '| ' + (($headerCells | ForEach-Object { '---' }) -join ' | ') + ' |'
            $mdTable = "$headerRow`n$sepRow"
            if ($caption) { $mdTable += "`n`n*$caption*" }
            $mdTable += "`n"
        } else {
            $mdTable = ''
        }

        $body = $body.Substring(0, $afStart) + $mdTable + $body.Substring($afEnd)
    }

    # Remove non-content elements
    $body = $body -replace '(?s)<div class="page-header">.*?</div>\s*</div>', ''
    $body = $body -replace '(?s)<div class="doc-nav">.*?</div>', ''
    $body = $body -replace '(?s)<div class="doc-footer">\s*(?:<p[^>]*>.*?</p>\s*)*</div>', ''
    $body = $body -replace '(?s)<div style="text-align: center;">\s*<a[^>]*class="ref-link"[^>]*>.*?</a>\s*</div>', ''
    $body = $body -replace '(?s)<!--.*?-->', ''
    $body = $body -replace '(?s)<script[^>]*>.*?</script>', ''

    # Convert headings
    $body = $body -replace '<h2>(.*?)</h2>', "`n### `$1`n"
    $body = $body -replace '<h3>(.*?)</h3>', "`n#### `$1`n"
    $body = $body -replace '<h4>(.*?)</h4>', "`n##### `$1`n"

    # Convert inline formatting
    $body = $body -replace '<strong>(.*?)</strong>', '**$1**'
    $body = $body -replace '<em>(.*?)</em>', '*$1*'
    $body = $body -replace '<code>(.*?)</code>', '`$1`'
    $body = $body -replace '<a[^>]*class="ref-inline"[^>]*><code>([^<]*)</code></a>', '`$1`'
    $body = $body -replace '<a[^>]*href="([^"]*)"[^>]*>(.*?)</a>', '$2'
    $body = $body -replace '<br\s*/?>', "`n"

    # Convert context-bar
    $body = [regex]::Replace($body, '(?s)<div class="context-bar">\s*(.*?)\s*</div>', {
        param($m)
        $content = $m.Groups[1].Value -replace '<[^>]+>', ''
        "> $($content.Trim())`n"
    })

    # Convert tables to markdown pipe tables
    $body = [regex]::Replace($body, '(?s)<table[^>]*>(.*?)</table>', {
        param($m)
        $tableHtml = $m.Groups[1].Value
        $rows = [regex]::Matches($tableHtml, '(?s)<tr[^>]*>(.*?)</tr>')
        $mdRows = @()
        $rowIndex = 0
        foreach ($row in $rows) {
            $cells = [regex]::Matches($row.Groups[1].Value, '(?s)<t[hd][^>]*>(.*?)</t[hd]>')
            $cleanCells = @()
            foreach ($cell in $cells) {
                $c = $cell.Groups[1].Value -replace '<[^>]+>', ''
                $c = $c.Trim() -replace '\|', '\|'
                $cleanCells += $c
            }
            $mdRows += '| ' + ($cleanCells -join ' | ') + ' |'
            if ($rowIndex -eq 0) {
                $mdRows += '| ' + (($cleanCells | ForEach-Object { '---' }) -join ' | ') + ' |'
            }
            $rowIndex++
        }
        ($mdRows -join "`n") + "`n"
    })

    # Strip all remaining HTML tags
    $body = $body -replace '<[^>]+>', ''

    # Entity decode - named entities
    $body = $body -replace '&mdash;', [char]0x2014
    $body = $body -replace '&ndash;', [char]0x2013
    $body = $body -replace '&rsquo;', [char]0x2019
    $body = $body -replace '&ldquo;', [char]0x201C
    $body = $body -replace '&rdquo;', [char]0x201D
    $body = $body -replace '&apos;', "'"
    $body = $body -replace '&rarr;', [char]0x2192
    $body = $body -replace '&larr;', [char]0x2190
    $body = $body -replace '&uarr;', [char]0x2191
    $body = $body -replace '&darr;', [char]0x2193
    $body = $body -replace '&nbsp;', ' '
    $body = $body -replace '&hellip;', [char]0x2026
    $body = $body -replace '&bull;', [char]0x2022
    $body = $body -replace '&middot;', [char]0x00B7
    $body = $body -replace '&times;', [char]0x00D7
    $body = $body -replace '&ge;', [char]0x2265
    $body = $body -replace '&eacute;', [char]0x00E9
    $body = $body -replace '&amp;', '&'
    $body = $body -replace '&lt;', '<'
    $body = $body -replace '&gt;', '>'
    # Entity decode - numeric entities
    $body = $body -replace '&#8212;', [char]0x2014
    $body = $body -replace '&#8211;', [char]0x2013
    $body = $body -replace '&#8217;', [char]0x2019
    $body = $body -replace '&#8220;', [char]0x201C
    $body = $body -replace '&#8221;', [char]0x201D
    $body = $body -replace '&#8594;', [char]0x2192
    $body = $body -replace '&#8592;', [char]0x2190
    $body = $body -replace '&#160;', ' '
    $body = $body -replace '&#8230;', [char]0x2026
    $body = $body -replace '&#8226;', [char]0x2022

    # Whitespace cleanup - strip leading spaces/tabs from each line (HTML indentation artifacts)
    $body = $body -replace '(?m)^[ \t]+', ''
    # Collapse 3+ consecutive newlines to 2
    $body = $body -replace '(?m)\n{3,}', "`n`n"
    $body = $body.Trim()

    return $body
}

# Renders a schema JSON reference into markdown tables and sections.
function Convert-JsonToMarkdown {
    param($JsonData)

    $md = ''

    # Tables
    foreach ($t in @($JsonData.tables)) {
        if (-not $t) { continue }
        $md += "`n### $($t.name)`n`n"

        if ($t.description) { $md += "$($t.description)`n`n" }
        if ($t.dataFlow) { $md += "**Data Flow:** $($t.dataFlow)`n`n" }

        foreach ($dn in @($t.designNotes)) {
            if (-not $dn) { continue }
            $topic = if ($dn.topic) { $dn.topic } else { 'Design Note' }
            $so = if ($dn.sortOrder) { " [sort:$($dn.sortOrder)]" } else { '' }
            $md += "**$topic`:**$so $($dn.note)`n`n"
        }

        # Columns
        $cols = @($t.columns)
        if ($cols -and $cols[0]) {
            $md += "**Columns:**`n`n"
            $md += "| Column | Type | Nullable | Default | Description |`n"
            $md += "| --- | --- | --- | --- | --- |`n"
            foreach ($c in $cols) {
                if (-not $c) { continue }
                $nullable = if ($c.nullable) { 'Yes' } else { 'No' }
                $ident = if ($c.identity) { ' (IDENTITY)' } else { '' }
                $dtype = if ($c.dataType) { $c.dataType } else { '' }
                if ($c.length) { $dtype += "($($c.length))" }
                # Default: strip outer parens, show IDENTITY for identity cols, em-dash for no default
                $dv = if ($c.default) { $c.default } else { '' }
                while ($dv.StartsWith('(') -and $dv.EndsWith(')')) { $dv = $dv.Substring(1, $dv.Length - 2) }
                $defaultDisplay = if ($c.identity) { 'IDENTITY' } elseif ($dv) { $dv } else { [char]0x2014 }
                $desc = if ($c.description) { $c.description -replace '\|', '\|' } else { '' }
                $md += "| $($c.name)$ident | $dtype | $nullable | $defaultDisplay | $desc |`n"
            }
            $md += "`n"
        }

        # Indexes
        foreach ($idx in @($t.indexes)) {
            if (-not $idx) { continue }
            $keyCols = if ($idx.keyColumns -is [array]) { $idx.keyColumns -join ', ' } else { $idx.keyColumns }
            $line = "  - **$($idx.name)** ($($idx.type)): $keyCols"
            $inc = if ($idx.includedColumns -is [array]) { $idx.includedColumns -join ', ' } else { $idx.includedColumns }
            if ($inc) { $line += " [includes: $inc]" }
            if ($idx.isPrimaryKey) { $line += " -- PRIMARY KEY" }
            $md += "$line`n"
        }
        if ($t.indexes) { $md += "`n" }

        # Check Constraints
        $checks = @($t.checkConstraints)
        if ($checks -and $checks[0]) {
            $md += "**Check Constraints:**`n`n"
            foreach ($ck in $checks) {
                if (-not $ck) { continue }
                $md += "  - **$($ck.name)**: ``$($ck.definition)```n"
            }
            $md += "`n"
        }

        # Foreign Keys
        $fks = @($t.foreignKeys)
        if ($fks -and $fks[0]) {
            $md += "**Foreign Keys:**`n`n"
            foreach ($fk in $fks) {
                if (-not $fk) { continue }
                $col = if ($fk.column) { $fk.column } else { '' }
                $refTable = if ($fk.referencedTable) { $fk.referencedTable } else { '' }
                $refCol = if ($fk.referencedColumn) { $fk.referencedColumn } else { '' }
                $md += "  - **$($fk.name)**: $col -> $refTable.$refCol`n"
            }
            $md += "`n"
        }

        # Status values
        $svs = @($t.statusValues)
        if ($svs -and $svs[0]) {
            $md += "**Status Values:**`n`n"
            $md += "| Column | Value | Meaning | Sort |`n"
            $md += "| --- | --- | --- | --- |`n"
            foreach ($sv in $svs) {
                if (-not $sv) { continue }
                $val = "$($sv.value)" -replace '\|', '\|'
                $meaning = "$($sv.meaning)" -replace '\|', '\|'
                $so = if ($sv.sortOrder) { $sv.sortOrder } else { '' }
                $md += "| $($sv.column) | $val | $meaning | $so |`n"
            }
            $md += "`n"
        }

        # Queries
        foreach ($q in @($t.queries)) {
            if (-not $q) { continue }
            $so = if ($q.sortOrder) { " [sort:$($q.sortOrder)]" } else { '' }
            $md += "**$($q.name)**$so"
            if ($q.description) { $md += " -- $($q.description)" }
            $md += "`n`n``````sql`n$($q.sql)`n```````n`n"
        }

        # Relationships
        $rels = @($t.relationshipNotes)
        if ($rels -and $rels[0]) {
            foreach ($rel in $rels) {
                if (-not $rel) { continue }
                $so = if ($rel.sortOrder) { " [sort:$($rel.sortOrder)]" } else { '' }
                $md += "  - **$($rel.relatedObject)**:$so $($rel.note)`n"
            }
            $md += "`n"
        }
    }

    # Procedures
    foreach ($p in @($JsonData.procedures)) {
        if (-not $p) { continue }
        $md += "`n### $($p.name)`n`n"

        if ($p.description) { $md += "$($p.description)`n`n" }
        if ($p.dataFlow) { $md += "**Data Flow:** $($p.dataFlow)`n`n" }

        foreach ($dn in @($p.designNotes)) {
            if (-not $dn) { continue }
            $topic = if ($dn.topic) { $dn.topic } else { 'Design Note' }
            $so = if ($dn.sortOrder) { " [sort:$($dn.sortOrder)]" } else { '' }
            $md += "**$topic`:**$so $($dn.note)`n`n"
        }

        $params = @($p.parameters)
        if ($params -and $params[0]) {
            $md += "**Parameters:**`n`n"
            $md += "| Parameter | Type | Direction | Default | Description |`n"
            $md += "| --- | --- | --- | --- | --- |`n"
            foreach ($pm in $params) {
                if (-not $pm) { continue }
                $direction = if ($pm.isOutput) { 'OUT' } else { 'IN' }
                $default = if ($pm.defaultValue) { $pm.defaultValue } else { '' }
                $desc = if ($pm.description) { $pm.description -replace '\|', '\|' } else { '' }
                $dtype = if ($pm.dataType) { $pm.dataType } else { '' }
                if ($pm.length) { $dtype += "($($pm.length))" }
                $md += "| $($pm.name) | $dtype | $direction | $default | $desc |`n"
            }
            $md += "`n"
        }

        foreach ($q in @($p.queries)) {
            if (-not $q) { continue }
            $so = if ($q.sortOrder) { " [sort:$($q.sortOrder)]" } else { '' }
            $md += "**$($q.name)**$so"
            if ($q.description) { $md += " -- $($q.description)" }
            $md += "`n`n``````sql`n$($q.sql)`n```````n`n"
        }

        foreach ($rel in @($p.relationshipNotes)) {
            if (-not $rel) { continue }
            $so = if ($rel.sortOrder) { " [sort:$($rel.sortOrder)]" } else { '' }
            $md += "  - **$($rel.relatedObject)**:$so $($rel.note)`n"
        }
        if ($p.relationshipNotes) { $md += "`n" }
    }

    # Triggers (DML - from sys.triggers/sys.tables)
    foreach ($tr in @($JsonData.triggers)) {
        if (-not $tr) { continue }
        $md += "`n### $($tr.name)`n`n"
        if ($tr.description) { $md += "$($tr.description)`n`n" }
        if ($tr.dataFlow) { $md += "**Data Flow:** $($tr.dataFlow)`n`n" }
        foreach ($dn in @($tr.designNotes)) {
            if (-not $dn) { continue }
            $topic = if ($dn.topic) { $dn.topic } else { 'Design Note' }
            $so = if ($dn.sortOrder) { " [sort:$($dn.sortOrder)]" } else { '' }
            $md += "**$topic`:**$so $($dn.note)`n`n"
        }
        foreach ($rel in @($tr.relationshipNotes)) {
            if (-not $rel) { continue }
            $so = if ($rel.sortOrder) { " [sort:$($rel.sortOrder)]" } else { '' }
            $md += "  - **$($rel.relatedObject)**:$so $($rel.note)`n"
        }
        if ($tr.relationshipNotes) { $md += "`n" }
    }

    # Functions
    foreach ($f in @($JsonData.functions)) {
        if (-not $f) { continue }
        $md += "`n### $($f.name)`n`n"
        if ($f.description) { $md += "$($f.description)`n`n" }
        if ($f.functionType) { $md += "**Type:** $($f.functionType)`n`n" }
        $params = @($f.parameters)
        if ($params -and $params[0]) {
            $md += "**Parameters:**`n`n"
            $md += "| Parameter | Type | Direction | Description |`n"
            $md += "| --- | --- | --- | --- |`n"
            foreach ($pm in $params) {
                if (-not $pm) { continue }
                $direction = if ($pm.isOutput) { 'OUT' } else { 'IN' }
                $desc = if ($pm.description) { $pm.description -replace '\|', '\|' } else { '' }
                $dtype = if ($pm.dataType) { $pm.dataType } else { '' }
                if ($pm.length) { $dtype += "($($pm.length))" }
                $md += "| $($pm.name) | $dtype | $direction | $desc |`n"
            }
            $md += "`n"
        }
    }

    # Views
    foreach ($v in @($JsonData.views)) {
        if (-not $v) { continue }
        $md += "`n### $($v.name)`n`n"
        if ($v.description) { $md += "$($v.description)`n`n" }
        $cols = @($v.columns)
        if ($cols -and $cols[0]) {
            $md += "**Columns:**`n`n"
            $md += "| Column | Type | Nullable |`n"
            $md += "| --- | --- | --- |`n"
            foreach ($c in $cols) {
                if (-not $c) { continue }
                $nullable = if ($c.nullable) { 'Yes' } else { 'No' }
                $dtype = if ($c.dataType) { $c.dataType } else { '' }
                if ($c.length) { $dtype += "($($c.length))" }
                $md += "| $($c.name) | $dtype | $nullable |`n"
            }
            $md += "`n"
        }
    }

    # Scripts
    foreach ($s in @($JsonData.scripts)) {
        if (-not $s) { continue }
        $md += "`n### $($s.name)`n`n"
        if ($s.description) { $md += "$($s.description)`n`n" }
        if ($s.dataFlow) { $md += "**Data Flow:** $($s.dataFlow)`n`n" }
        foreach ($dn in @($s.designNotes)) {
            if (-not $dn) { continue }
            $topic = if ($dn.topic) { $dn.topic } else { 'Design Note' }
            $so = if ($dn.sortOrder) { " [sort:$($dn.sortOrder)]" } else { '' }
            $md += "**$topic`:**$so $($dn.note)`n`n"
        }
        foreach ($q in @($s.queries)) {
            if (-not $q) { continue }
            $so = if ($q.sortOrder) { " [sort:$($q.sortOrder)]" } else { '' }
            $md += "**$($q.name)**$so"
            if ($q.description) { $md += " -- $($q.description)" }
            $md += "`n`n``````sql`n$($q.sql)`n```````n`n"
        }
        foreach ($rel in @($s.relationshipNotes)) {
            if (-not $rel) { continue }
            $so = if ($rel.sortOrder) { " [sort:$($rel.sortOrder)]" } else { '' }
            $md += "  - **$($rel.relatedObject)**:$so $($rel.note)`n"
        }
        if ($s.relationshipNotes) { $md += "`n" }
    }

    # XE Sessions
    foreach ($x in @($JsonData.xeSessions)) {
        if (-not $x) { continue }
        $md += "`n### $($x.name)`n`n"
        if ($x.description) { $md += "$($x.description)`n`n" }
        if ($x.dataFlow) { $md += "**Data Flow:** $($x.dataFlow)`n`n" }
        foreach ($dn in @($x.designNotes)) {
            if (-not $dn) { continue }
            $topic = if ($dn.topic) { $dn.topic } else { 'Design Note' }
            $so = if ($dn.sortOrder) { " [sort:$($dn.sortOrder)]" } else { '' }
            $md += "**$topic`:**$so $($dn.note)`n`n"
        }
        foreach ($q in @($x.queries)) {
            if (-not $q) { continue }
            $so = if ($q.sortOrder) { " [sort:$($q.sortOrder)]" } else { '' }
            $md += "**$($q.name)**$so"
            if ($q.description) { $md += " -- $($q.description)" }
            $md += "`n`n``````sql`n$($q.sql)`n```````n`n"
        }
        foreach ($rel in @($x.relationshipNotes)) {
            if (-not $rel) { continue }
            $so = if ($rel.sortOrder) { " [sort:$($rel.sortOrder)]" } else { '' }
            $md += "  - **$($rel.relatedObject)**:$so $($rel.note)`n"
        }
        if ($x.relationshipNotes) { $md += "`n" }
    }

    # DDL Triggers
    foreach ($d in @($JsonData.ddlTriggers)) {
        if (-not $d) { continue }
        $md += "`n### $($d.name)`n`n"
        if ($d.description) { $md += "$($d.description)`n`n" }
        if ($d.dataFlow) { $md += "**Data Flow:** $($d.dataFlow)`n`n" }
        foreach ($dn in @($d.designNotes)) {
            if (-not $dn) { continue }
            $topic = if ($dn.topic) { $dn.topic } else { 'Design Note' }
            $so = if ($dn.sortOrder) { " [sort:$($dn.sortOrder)]" } else { '' }
            $md += "**$topic`:**$so $($dn.note)`n`n"
        }
        foreach ($rel in @($d.relationshipNotes)) {
            if (-not $rel) { continue }
            $so = if ($rel.sortOrder) { " [sort:$($rel.sortOrder)]" } else { '' }
            $md += "  - **$($rel.relatedObject)**:$so $($rel.note)`n"
        }
        if ($d.relationshipNotes) { $md += "`n" }
    }

    return $md
}

# Converts a standalone guide page to markdown, stripping guide-specific visual elements before delegating to the narrative markdown converter.
function Convert-GuideToMarkdown {
    param([string]$HtmlContent)

    $body = $HtmlContent

    # Strip guide-specific elements before standard conversion

    # Embedded <style> block
    $body = $body -replace '(?s)<style>.*?</style>', ''

    # Guide mockup blocks - depth-counted removal
    while ($body.Contains('<div class="guide-mock"')) {
        $gmStart = $body.IndexOf('<div class="guide-mock"')
        if ($gmStart -eq -1) { break }
        $depth = 0; $si = $gmStart; $gmEnd = -1
        while ($si -lt $body.Length) {
            $no = $body.IndexOf('<div', $si + 1)
            $nc = $body.IndexOf('</div>', $si + 1)
            if ($nc -eq -1) { break }
            if ($si -eq $gmStart) { $depth = 1; $si = $gmStart + 1; continue }
            if ($no -ne -1 -and $no -lt $nc) { $depth++; $si = $no + 1 }
            else { $depth--; if ($depth -eq 0) { $gmEnd = $nc + 6; break }; $si = $nc + 1 }
        }
        if ($gmEnd -eq -1) { break }
        $body = $body.Substring(0, $gmStart) + $body.Substring($gmEnd)
    }

    # Jump nav
    $body = $body -replace '(?s)<div class="guide-jump-nav">.*?</div>', ''

    # Step number badges - convert to plain number
    $body = $body -replace '<span class="step-num-badge">(\d+)</span>\s*', '$1. '

    # Guide step wrapper divs
    $body = $body -replace '<div class="guide-step"[^>]*>', ''

    # Inline visual elements
    $body = $body -replace '<span class="key-item">(.*?)</span>', '$1'
    $body = $body -replace '<span class="mock-env-badge[^"]*">([^<]*)</span>', '$1'
    $body = $body -replace '<span class="mock-badge-fv">([^<]*)</span>', '($1)'
    $body = $body -replace '<span class="mock-chip[^"]*">([^<]*(?:<[^>]+>[^<]*)*)</span>', '$1'
    $body = $body -replace '<span class="mock-chip-nullify"[^>]*>[^<]*</span>', ''
    $body = $body -replace '<span class="mock-chip-required"[^>]*>[^<]*</span>', ''

    # Standalone breadcrumb nav
    $body = $body -replace '(?s)<div class="doc-nav">\s*<a[^>]*>.*?</a>\s*<span[^>]*>.*?</span>\s*<span[^>]*>.*?</span>\s*</div>', ''

    # Strip ALL remaining span tags (keep content, remove tags)
    $body = $body -replace '<span[^>]*>', ''
    $body = $body -replace '</span>', ''

    # Extract page-wrapper content
    # Guide pages have no <script> tag, so the standard converter's extraction
    # regex won't match. Extract here before delegating.
    if ($body -match '(?s)<div class="page-wrapper">(.*)</div>\s*</body>') {
        $body = '<div class="page-wrapper">' + $Matches[1] + '</div>'
    }

    # Delegate to standard narrative markdown converter
    return Convert-HtmlToMarkdown -HtmlContent $body
}

# Converts a Control Center guide page to markdown with its own element handling.
function Convert-CcGuideToMarkdown {
    param([string]$HtmlContent)

    # Standalone markdown converter for CC guide pages. Extracts the About
    # intro, Key flip cards, and Guide sections; strips header, nav, mockup,
    # sidebar items, footer, slideout, and scripts. Does not pass through
    # Convert-HtmlToMarkdown.

    $md = ''

    # Extract title
    $title = ''
    if ($HtmlContent -match '<h1>(.*?)</h1>') {
        $title = $Matches[1] -replace '<[^>]+>', ''
        # Decode entities in title
